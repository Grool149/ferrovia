<p class="titre">Liens</p>
<p>Voici une liste non exhaustive de sites et fora de discussion qui peuvent int&eacute;resser l'amateur de chemins de fer en g&eacute;n&eacute;ral et celui de simulation ferroviaire en particulier.<br />
Cette liste est forc&eacute;ment subjective puisqu'elle s'oriente davantage vers le mat&eacute;riel roulant plut&ocirc;t que vers les lignes et se limite (pour le moment) &agrave; la SNCF.</p>
<p>Internet est malheureusement trop &eacute;ph&eacute;m&egrave;re. Si cette r&eacute;activit&eacute; permet parfois de voir fleurir de nouveaux sites int&eacute;ressants, elle cause aussi la perte brutale et sans signe avant coureur de certaines p&eacute;pites.</p>
<p>Cette page est mise &agrave; jour autant que possible, mais depuis une quinzaine d'ann&eacute;es que MSTS fut lanc&eacute;, nombreux sont les sites proposant du mat&eacute;riel ou des lignes qui ont disparu. Plus grave encore les fora de discussion qui ferment et emportent avec eux autant d'information qu'une biblioth&egrave;que qui br&ucirc;le. On peut ainsi regretter la disparition de certains sites marchands tels que les TGV Speedworks ou la soci&eacute;t&eacute; Sylium, mais aussi des sites personnels tels que ceux de VH24, Belph&eacute;gor ou plus r&eacute;cemment Microrail. De nombreux sites ne sont plus mis &agrave; jour depuis de longues ann&eacute;es car leurs cr&eacute;ateurs ne s'adonnent plus &agrave; la simulation. Remercions-les d'avoir n&eacute;anmoins maintenu leurs sites en ligne. </p>

<p class="soustitre"><br />Trains R&eacute;els</p>
<p><span class="Etiquette"><a href="http://pagesperso-orange.fr/trainsso/" class="link1">Trains du Sud-Ouest</a></span>
Site de référence pour les inventaires régulièrement mis à jour du matériel moteur SNCF.</p>
<p><span class="Etiquette"><a href="http://pagesperso-orange.fr/florent.brisou" class="link1">Rail 21</a></span>
Site personnel d'ingénieur freiniste d'Alstom. Fiches techniques détaillées du matériel roulant et du réseau.</p>
<p><span class="Etiquette"><a href="http://www.railsavoie.org/" class="link1">Rail-Savoie</a></span>
Spécialisé sur le réseau savoyard. De beaux documents sur l'épopée des protos monophasés et l'actu de l'APMFS.</p>
<p><span class="Etiquette"><a href="http://www.trains-europe.fr/" class="link1">Trains-Europe</a></span>
Site encyclop&eacute;dique sur le mat&eacute;riel roulant.</p>
<p><span class="Etiquette"><a href="http://www.railfaneurope.net/pix/pixtxt.html" class="link1">RailFanEurope</a></span>
Ant&eacute;rieurement connue sous le nom Mercurio, cette base de donn&eacute;es de photos du mat&eacute;riel europ&eacute;en est indispensable.</p>
<p class="soustitre"><br />Forums</p>
<p><span class="Etiquette"><a href="http://forum.e-train.fr/" class="link1">Loco-Revue</a></span>
Bien que d&eacute;di&eacute; en priorit&eacute; au mod&eacute;lisme, le forum de Loco-Revue regorge d'informations tr&egrave;s pointues sur les chemins de fer, dans tous les domaines. La r&eacute;putation de compteurs de rivets des mod&eacute;listes n'est pas usurp&eacute;e.</p>
<p><span class="Etiquette"><a href="http://www.forum-train.fr/forum/" class="link1">Forum-train</a></span>
Cr&eacute;&eacute; par quelques mod&eacute;listes issus du forum pr&eacute;c&eacute;dent. Contient aussi des sujets richement document&eacute;s.</p>
<p><span class="Etiquette"><a href="http://histoire.trains-en-vadrouille.com/" class="link1">Les Trains de l'Histoire</a></span>
Parti des archives du forum "Trains en voyage". Spécialisé dans le Train d'avant 1990. (inscription obligatoire pour acc&eacute;der aux sujets)</p>
<p><span class="Etiquette"><a href="http://www.trains-en-voyage.com/forum/" class="link1">Trains en voyage</a></span>
Forum de photographes et d'amateurs éclairés de chemins de fer. (inscription obligatoire pour acc&eacute;der aux sujets)</p>
<p><span class="Etiquette"><a href="http://www.cheminots.net/forum/" class="link1">Le web des cheminots</a></span>
Forum de cheminots. Ambiance professionnelle qui donne un bon aperçu de ce qu'il se passe dans la grande Maison.</p>
<p><span class="Etiquette"><a href="http://www.trainsfrancais.com/forum/index.php" class="link1">Trains Fran&ccedil;ais</a></span>
Forum d'actualités et de photo ferroviaire.</p>
<p><span class="Etiquette"><a href="http://forums.photos-de-trains.net/index.php" class="link1">Photos-de-trains</a></span>
Ce forum, dorénavant fermé mais toujours accessible en lecture, était consacré purement à la photo ferroviaire, avec un haut niveau d'exigence</p>

