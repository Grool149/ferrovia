    <td valign="top">
      <p class="text">Les X 2700, plus souvent appel&eacute;s RGP (Rames &agrave; Grand Parcours) sont des autorails bicaisse unifi&eacute;s de 600ch puis 825 ch form&eacute;s d'une motrice X 2700 et d'une remorque XR 7700 &eacute;quip&eacute;e d'une loge de conduite. Exclusivement premi&egrave;re classe, ils se chargeaient de trains parmi les plus prestigieux dans les ann&eacute;es 1950. Leur livr&eacute;e vert d'eau et cr&ecirc;me, r&eacute;hauss&eacute;e d'enjoliveurs en aluminium les distinguait des autres autorails plus rustiques. </p>
      <p class="text">Deux s&eacute;ries fortement diff&eacute;rentes forment la famille des RGP : les RGP 1 et les RGP 2. Aucun rapport avec la chronologie puisque les RGP 2 ont &eacute;t&eacute; construites les premi&egrave;res. En fait, le chiffre indique le nombre de moteurs qui animent la motrice. Les premi&egrave;res RGP sont directement d&eacute;riv&eacute;es des <a href="/tr/2400.php" class="link1">X 2400</a> et en reprennent la disposition m&eacute;canique : deux V12 Renault 517 de 300ch chacun accoupl&eacute;s &agrave; une boite de vitesse m&eacute;canique. L'implantation est quelque peu diff&eacute;rente puisque le second groupe moteur-boite est retourn&eacute; pour m&eacute;nager un couloir d'acc&egrave;s &agrave; la remorque. Parall&egrave;lement &agrave;  l'&eacute;volution de l'X 2400 bi-moteur vers l'<a href="/tr/2800.php" class="link1">X 2800</a> monomoteur, la seconde s&eacute;rie de RGP fut &eacute;quip&eacute;e d'un unique V12 MGO de 825ch et d'une boite &agrave; convertisseur hydraulique M&eacute;kydro.</p>
      <p class="text">Les remorques XR 7700 pr&eacute;sentent la particularit&eacute; de  poss&eacute;der une loge de conduite d&eacute;cal&eacute;e sur la gauche, le car&eacute;nage central est amovible et un soufflet peut-&ecirc;tre d&eacute;pli&eacute;. Deux rames RGP peuvent former une rame de quatre caisses (voire cinq en intercalant une troisi&egrave;me remorque) interconnect&eacute;es.</p>
      <p class="text">Une sous-s&eacute;rie de RGP 1 fut d&eacute;di&eacute;e aux Trans Europ Express sur des relations internationales avec la Belgique, l'Allemagne, la Suisse et l'Italie. Num&eacute;rot&eacute;es X 2770, ces rames avaient une livr&eacute;e sp&eacute;cifique, le vert d'eau &eacute;tant remplac&eacute; par un rouge grenat.</p>
      <p class="text"> Les RGP furent progressivement d&eacute;class&eacute;es des trains les plus nobles au profit de rames tract&eacute;es ou de Turbotrains. Elles &eacute;taient affect&eacute;es &agrave; des trains automoteurs interr&eacute;gionaux tels que la Fl&egrave;che d'Argent ou l'Alpazur. La livr&eacute;e adopta des coloris similaires aux RTG, orange et gris m&eacute;tallis&eacute;. L'int&eacute;rieur fut r&eacute;am&eacute;nag&eacute; en deux classes. </p>
      <p class="text">Tout comme les X 2400, le co&ucirc;t d'entretien &eacute;lev&eacute; des RGP 2 leur fut fatal dans les ann&eacute;es 1980. Seules les X 2707 et 2714 furent conserv&eacute;es et fortement modifi&eacute;es pour former la rame sp&eacute;ciale V4 d'auscultation des voies. Les RGP 1 ont en revanche b&eacute;n&eacute;fici&eacute; d'une r&eacute;novation lourde avec reconstruction int&eacute;grale des deux cabines de conduite. Les deux extr&eacute;mit&eacute;s ayant d&eacute;sormais un aspect identique, l'intercirculation entre deux remorques fut supprim&eacute;e.</p>
      <p class="text">Les RGP modernis&eacute;es re&ccedil;urent la nouvelle livr&eacute;e  TER jaune, bleue ou rouge en fonction de leur r&eacute;gion d'affectation et se sp&eacute;cialis&egrave;rent dans les trains r&eacute;gionnaux sur les lignes montagneuses.</p>
