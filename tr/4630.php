<p class="titre">X 4630 - 94630</p>
<p>Les X 4630 sont &eacute;troitement d&eacute;riv&eacute;s des <a href="/tr/4300.php" class="link1">X 4500</a>. Ils en reprennent le moteur Saurer de 450 chevaux mais la transmission fait appel &agrave; un convertisseur hydraulique Voith.</p>
<p>La num&eacute;rotation de la s&eacute;rie X 4630 est curieuse puisqu'elle ne commence pas &agrave; un nombre rond, cependant il y a bien un trou entre le dernier X 4500 (X 4623) livr&eacute; le 5 juin 1970 et l'X 4630 (la s&eacute;rie ne d&eacute;bute pas &agrave; 4631) livr&eacute; le 12 f&eacute;vrier 1971. Les remorques XR 8300 et XR 8500 sont communes &agrave; toutes les s&eacute;ries d'X 4300, 4500 et 4630.</p>
<p>113 exemplaires furent construits, auxquels s'ajoutent l'X 4609 transform&eacute; en X 4743 et l'X 94630, identique aux 4630 mais exploit&eacute; exclusivement sur la ligne Cannes-Ranguin avant d'&ecirc;tre revendu &agrave; la SNCF en 1995, qui l'a transform&eacute; en X 4744.</p>
<p>Les X4630 n'ont pas eu beaucoup de sursis par rapport aux s&eacute;ries &agrave; bo&icirc;te m&eacute;canique. Alors que des radiations ponctuelles commen&ccedil;aient en 2003 sur les &quot;Ambulances&quot; assez d&eacute;t&eacute;rior&eacute;es par l'air salin nantais, les radiations massives ont d&eacute;but&eacute; en 2008 et 2009 avec l'arriv&eacute;e en force des AGC. Les derniers &eacute;l&eacute;ments en service commercial furent mis &agrave; la retraite en f&eacute;vrier 2012. Il ne subsiste que trois &eacute;l&eacute;ments &agrave; l'inventaire, tous au service de l'INFRA (X 4630, 4652, 4670).</p>
<p>Les X 4630 ne sont pas tous partis &agrave; la casse pour autant. Quarante X 4630 poursuivent une seconde carri&egrave;re en Roumanie. <br />
Parmi les engins pr&eacute;serv&eacute;s en France, on compte :<br />
L'X4647  pr&eacute;serv&eacute;s par l'ABFC.<br />
L'X4719 par le CFT Sud des Ardennes<br />
Les X4709 et 4730 par le Train du Pays Cathare et du Fenouill&egrave;des<br />
L'X4691 par l'Amicale de la ligne Caen-Flers</p>
<p class="titre2">Les Livr&eacute;es</p>
<p>En dehors de la livr&eacute;e classique rouge et cr&egrave;me, les X 4630 ont port&eacute; des livr&eacute;es plus atypiques.</p>
<p>Les rames affect&eacute;es &agrave; la banlieue Nantaise ont re&ccedil;u une livr&eacute;e similaire &agrave; celle du tramway TAN, blanche r&eacute;hauss&eacute;e d'une fine bande bleue et d'un liseret pointill&eacute; vert en limite de pavillon. Ce qui leur a valu le surnom d'&quot;Ambulances&quot;.</p>
<p>Plusieurs engins ne b&eacute;n&eacute;ficiant pas d'une r&eacute;novation lourde ont n&eacute;anmoins &eacute;t&eacute; repeints avec les coloris TER gris m&eacute;tallis&eacute; et bleu marine. Les premiers entretenus &agrave; Nevers reprenant le sch&eacute;ma de peinture habituel des EAD, les autres g&eacute;r&eacute;s par Lyon Vaise adoptant un dessin proche des automoteurs TER de nouvelle g&eacute;n&eacute;ration.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur : ANF<br />
Motorisation : Saurer SDHR<br />
Puissance : 330 kW<br />
Vitesse limite : 120 km/h<br />
Masse : 63,1t</p>
<p>Longueur : 42,480 m</p>
