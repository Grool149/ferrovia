<p><span class="titre">CC 40100</span><br />
<span class="soustitre">L'Europe avant l'heure</span></p>
<p>Les CC 40100 sont des machines quadritension con&ccedil;ues pour les relations TEE en 1964. Elles se sont par la suite sp&eacute;cialis&eacute;es sur l'axe PBA : Paris-Bruxelles-Amsterdam jusqu'&agrave; la mise en service du Thalys en 1996. </p>
<p>Construites &agrave; 11 exemplaires (la CC 40106 fut d&eacute;truite dans un accident. Une seconde machine fut reconstruite &agrave; partir d'un chaudron de s&eacute;rie 18 SNCB).</p>
<p>Machines pr&eacute;serv&eacute;es : 40101 &agrave; la Cit&eacute; du Train, 40109 au CMCF Denis Papin de Oignies, 40110 au MFPN, Paris La Chapelle</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alsthom, Jeumont<br />
Tension d'alimentation: monophas&eacute; 25 kV, 50 Hz, 15 kV 16,7 Hz; Courant continu sous 1,5 et 3 kV<br />
Cha&icirc;ne de traction: Auto-transformateur, redresseur &agrave; diodes, commande par rh&eacute;ostat<br />
Motorisation: un moteur &agrave; courant redress&eacute; par bogie<br />
Puissance totale: 3670 kW ou 4480 kW selon les sous-s&eacute;ries</p>
<p>Longueur:  22,03m<br />
Masse: 109,6t<br />
Pantographes: 4</p>
