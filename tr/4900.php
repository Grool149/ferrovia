<p><span class="titre">X 4900</span><br />
<span class="soustitre">Les EAT</span></p>
<p>Les X 4900 ou EAT pour El&eacute;ments Automoteurs Tricaisses sont des versions surmotoris&eacute;s des <a href="/tr/4630.php" class="link1">X 4630</a>. Deux motrices de type 4630 (moteur Saurer de 330 kW et convertisseur de couple Voith) encadrent une remorque sans cabine. Destin&eacute;s &agrave; des relations plus longues que les autres Caravelles, les X 4900 disposaient de si&egrave;ges individuels comme les <a href="/tr/4750.php" class="link1">X 4790</a>. </p>
<p>13 rames ont &eacute;t&eacute; construites de 1975 &agrave; 1977.<br />Le rapport poids-puissance de ces engins leur permet une vitesse limite de 140 km/h.</p>
<p>Deux types d'usage &eacute;taient pr&eacute;vus &agrave; l'origine. La desserte de ligne au profil  difficile (Marseille - Brian&ccedil;on, Marseille - Veyne - Grenoble) et des relations d'autorails express en Normandie. Ils assuraient notamment des Caen-Tours, Caen-Rennes. Ils sont d&eacute;sormais sur la ligne Rouen-Dieppe. </p>
<p>Alors que la plupart des Caravelles &eacute;taient amorties, les X 4900 ont b&eacute;n&eacute;fici&eacute; d'un r&eacute;pit et la totalit&eacute; de la s&eacute;rie a &eacute;t&eacute; lourdement modernis&eacute;e avec des cabines renforc&eacute;es.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur : ANF<br />
Motorisation : 2 x Saurer SDHR<br />
Puissance: 660 kW<br />
Vitesse limite: 140 km/h<br />
Masse: 103,8t<br />
Longueur: 62,230 m; 64,230m apr&egrave;s modernisation</p>
