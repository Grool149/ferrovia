    <td valign="top">
      <p class="text">Les Z 6400 sont des &eacute;l&eacute;ments automoteurs monocourants fonctionnant sous courant alternatif 25 kV 50 Hz con&ccedil;us initialement pour la desserte de l'a&eacute;roport de Roissy et l'&eacute;quipement de la banlieue Ouest Parisienne en remplacement des rames standard aliment&eacute;es par troisi&egrave;me rail 750V. Suite &agrave; la mise en service de l'interconnexion du RER B, Les Z 6400 ont quitt&eacute; Paris-Nord pour toutes se retrouver sur les groupes II et III de Paris-St Lazare.</p>
      <p class="text">Les Z 6400 ont &eacute;t&eacute; construites &agrave; 75 exemplaires de 1976 &agrave; 1979. Leur chaudron fait massivement appel &agrave; l'acier inoxydable comme leurs pr&eacute;d&eacute;cesseurs <a href="/tr/6100.php" class="link1">Z 6100</a> ou <a href="/tr/6300.php" class="link1">6300</a> mais les dimensions g&eacute;n&eacute;reuses et l'am&eacute;nagement en si&egrave;ges individuels en font des automotrices r&eacute;solument modernes. La composition de la rame adopte une disposition &quot;muscl&eacute;e&quot; de deux motrices &agrave; adh&eacute;rence totale encadrant deux remorques. Sch&eacute;ma qui sera repris par les <a href="/tr/mi79.php" class="link1">Z 8100</a> et les <a href="/tr/20500.php" class="link1">Z2N</a> et qui permet &agrave; ces rames d'&ecirc;tre toujours tr&egrave;s performantes apr&egrave;s plus de 30 ans de carri&egrave;re. </p>
      <p class="text">La rame Z 6483/84 (une des trois rames transform&eacute;es Grande ceinture) a &eacute;t&eacute; radi&eacute;e suite &agrave; un incendie en d&eacute;cembre 2011. </p>
      <p class="text"><span class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> Livr&eacute;es</span></p>
      <p class="text">Les Z 6400 poss&eacute;daient une livr&eacute;e qui leur &eacute;tait propre. L'inox nu &eacute;tait r&eacute;hauss&eacute; d'un bandeau bleu au niveau des baies. A l'avant, une moustaches jaune portait le sigle SNCF de type 'UIC'.</p>
      <p class="text">Lors de leur r&eacute;novation caisse au d&eacute;but des ann&eacute;es 2000, les Z 6400 ont re&ccedil;u une livr&eacute;e &agrave; mi-chemin entre la livr&eacute;e Ile-de-France &eacute;quipant les RER et trains  de banlieue SNCF et la livr&eacute;e Transilien des <a href="/tr/22500.php" class="link1">Z 22500</a>, avec la moustache peinte en bleu clair.</p>
      <p class="text">Trois rames modifi&eacute;es pour la remise en service de la Grande Ceinture Ouest adoptent la nouvelle livr&eacute;e Transilien bleue &agrave; berlingots de couleurs. La Z 6483/84 est radi&eacute;e suite &agrave; incendie. </p>
