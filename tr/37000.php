<p><span class="titre">BB 37000 - E 37500</span><br />
<span class="soustitre">Prima internationale</span></p>
<p>Les BB 37000 sont des machines d&eacute;riv&eacute;es directement des locomotives Fret <a href="/tr/27000.php" class="link1">BB 27000</a>. Leur &eacute;quipement de traction et de signalisation embarqu&eacute;e leur permet de rouler sous 15kV 16,7Hz en Allemagne ou en Suisse. Elles ont notamment remplac&eacute; les <a href="/tr/20200.php" class="link1">BB 20200</a> sur les trains internationaux du noeud de B&acirc;le.</p>
<p>Une partie des BB 37000 appartient &agrave; Akiem, filiale de la SNCF qui louent des locomotives &agrave; des entreprises ferroviaires priv&eacute;es.</p>
<p>60 machines ont été mises en service de 2004 à 2006. Cependant, la 37007 fut détruite le 11 octobre 2006 dans la catastrophe de Zoufftgen.</p>
<p>Alstom a également vendu 31 machines identiques directement à des opérateurs privés (Véolia, devenu Europorte après rachat par Eurotunnel, et le loueur CB Rail) sous le numéro E 37500.
La SNCF, via Akiem, gère certaines E 37500 (37511, 37530).</p>
<p class="titre2">Les Livr&eacute;es</p>
<p>La livr&eacute;e de base est Fret semblable &agrave; celle des BB 27000 au num&eacute;ro pr&egrave;s.</p>
<p>Les engins Akiem ont &eacute;t&eacute; repeints en gris aluminium.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom<br />
Tension d'alimentation: monophas&eacute; 25 kV, 50 Hz et 15 kV, 16.7 Hz; Courant continu sous 1,5kV<br />
Cha&icirc;ne de traction: onduleurs &agrave; transistors IGBT - Moteur asynchrones<br />
Motorisation: 2 moteurs asynchrones suspendus par le nez par bogie<br />
Puissance totale: 4200 kW</p>
<p>Longueur:  19,52m<br />
Masse: 89t</p>
