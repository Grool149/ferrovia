<p><span class="titre">CC 72000</span></p>
<p>Le d&eacute;but des années 60 voit le développement des premières machines 
diésel à moteur industriel de forte puissance: les <a class="link1" href="67000.php">BB 
67000</a> et leurs cousines <a href="/tr/68000.php" class="link1">A1A-A1A 68000/68500</a> ; ainsi que l'ébauche 
de la chaine de traction triphasée asynchrone des futures <a class="link1" href="67300.php">BB 
67300</a>. Toutefois, ces solutions ne développant au mieux que 1765 kW 
ne permettent pas de remplacer les plus puissantes vapeurs sur les services 
haut de gamme. De plus, alors que la traction électrique relève la vitesse 
des axes principaux, les machines diésel de l'époque ne font guère mieux 
que les vapeurs qu'elles doivent chasser au nom du progrès.</p>
<p> L'étude de machines dépassant les 2000 kW conduit aux prototypes bimoteurs 
BB 69000, à transmission hydraulique et CC 70000 à transmission électrique 
triphasée asynchrone. Cette dernière solution semble la plus intéressante 
mais la mise à disposition de moteurs à très forte puissance vint modifier 
le choix technique qui sera adopté pour les futures CC 72000. Elles conserveront 
une chaine de traction triphasée et les bogies C à double rapport de réduction 
mais la motorisation sera dérivée du moteur V12 AGO des <a href="/tr/68000.php" class="link1">A1A-A1A 68500</a>, 
porté à 16 cylindres et qui développe 2650 kW. </p>
<p>Les CC 72000 ont bénéficié du développement conjoint des <a href="6500.php" class="link1">CC 
6500</a> pour de nombreux organes électriques et mécaniques. Elles furent 
mises en service à partir de fin 1967 et surclassèrent déjà les autres 
machines monomoteurs agées de quatre ans à peine. La seconde sous-série 
(72021 et suivantes), livrée à partir de 1969 et dotée d'un rapport de 
réduction GV plus long permit progressivement le passage à la vitesse 
limite de 160 km/h (à partir de 1972), à l'égal des machines électriques 
(si l'on occulte les élitistes TEE V200). En 1991, la CC 72017 fut, à 
la suite d'un incendie et d'une longue remise en état, équipée du même 
rapport. Les 72021 à 92 possèdent également des équipements électriques 
différents (notamment le dispositif d'excitation et les ponts redresseurs). 
Afin de préserver les CC 72000, l'utilisation du moteur fut limitée en 
puissance à 2320 kW au début des années 90. </p>
<p>Au cours du premier semestre 1999, la CC 72062 fut modifiée pour tirer 
un TGV en service commercial. Les tampons furent déposés et laissèrent 
la place à un attelage Scharffenberg spécialement équipé pour alimenter 
les auxiliaires de la rame TGV en courant. Objectif: desservir La Roche 
sur Yon et les Sables d'Olonnes par TGV direct depuis Paris alors que 
la ligne n'&eacute;tait pas électrifiée à partir de Nantes (travaux prévus pour 
2006). Après les essais, les 72061 et 64 furent également modifées début 
2000 pour commencer le service commercial au mois de Mai. Ces bètes curieuses 
&eacute;taient cependant une solution bancale qui revenait très cher. De rudes négociations 
entre la SNCF et les collectivités locales ont permit de ne pas arrêter 
ce service en Septembre 2003. Cependant, le changement de majorit&eacute; 
au Conseil R&eacute;gional des Pays de la Loire en 2004 coupa court &agrave; 
ce qui ressemble aujourd'hui plus &agrave; un caprice politique qu'&agrave; 
une v&eacute;ritable prouesse technique. Les derniers TGV tract&eacute;s 
des Sables d'Olonnes ont roul&eacute; en d&eacute;cembre 2004. Les trois 
CC 72000 &eacute;tant remis au type. Depuis l'&eacute;lectrification est termin&eacute;e et les TGV reviennent aux Sables d'Olonnes. </p>
<p>Les machines desservant la ligne Paris-Bâle posaient problème depuis 
quelques années. Leur préchauffage au dépôt de la Villette irritant les 
riverains. Les tractations entre associations et SNCF débouchèrent sur 
un projet de remotorisation d'un lot de 30 CC 72000. Elles sont devenues 
des <a class="link1" href="72100.php">CC 72100</a> entre 2002 et 2004.</p>
<p class="titre2">Les livr&eacute;es</p>
<p>La livrée classique bleu pétrole des 72000 n'a que peu évolué depuis 
la sortie d'usine (suppression des plaques en relief, estompage du macaron 
et du numéro frontaux qui rendent leur allure un peu triste). A partir 
de 1995, la livrée Multiservice est appliquée tout d'abord en essai sur 
la CC 72006 à base de bleu isabelle (comme sur la <a class="link1" href="67300.php">BB 
67373</a>) puis à base de rouge sur 5 machines 72030, 37, 40, 68 et 78. 
Ces livrées ne perdureront pas puisque la livrée Fret, apparue à partir 
de l'an 2000, a été appliquée depuis à la 72006 et que les 5 autres machines 
furent transformées en <a class="link1" href="72100.php">CC 72100</a> 
revêtant, de ce fait, la livrée "En voyage". Cette même livrée est également 
arborée par les quelques 72000 Grandes Lignes passées récemment en révision. 
Notamment les 72045, 58, 60 et 49. Toutefois, les 72045, 58 et 60 ont 
aussi &eacute;t&eacute; transform&eacute;es en <a href="72100.php" class="link1">72100</a>.</p>
<p class="titre2">La carri&egrave;re</p>
<p>Au cours de leur carrière, les CC 72000 ont essentiellement desservi 
les grandes radiales non électrifiées (Le Mans => Nantes / Quimper / Brest, 
Paris => Clermont-Ferrand, Paris => Bâle, Amiens => Calais) et la transversale 
Nantes => Lyon => Grenoble ainsi que des services locaux en Rhone-Alpes, 
en Lorraine et dans le Massif Central. Les importants programmes d'électrification 
et l'arrivée d'automoteurs modernes ont réduit leur champ d'action aux 
axes Paris-Laon, Paris-Bâle et Tours-Lyon-Grenoble. Mais de nouveaux besoins 
ont émerg&eacute; : l'anecdotique service du TGV Vendée mais aussi le remplacement 
des A1A-A1A 68000/68500 en service Fret, en Normandie notamment. Les &eacute;tablissements 
de Nevers et Chalindrey se partagaient le parc, mais Chalindrey ne conserve 
plus que des machines remotoris&eacute;es.</p>
<p> Il est à noter que malgré leur statut de locomotives diésel Françaises 
les plus puissantes, leurs 2250 kW demeurent finalement très modestes 
comparés aux locomotives électriques dont la puissance massique est double 
voire triple. Aussi, les services les plus chargés leur ont &eacute;t&eacute; 
repris par des UM de <a class="link1" href="67400.php">BB 67400</a> plus 
puissantes mais aussi plus chères en exploitation.</p>
<p> L'amortissement de la s&eacute;rie a commenc&eacute; par les 72011 et 72012 de l'activit&eacute; 
Fret au cours de l'ann&eacute;e 2004. Apr&egrave;s 40 ans 
de service, les CC 72000 cessent toute activit&eacute; en D&eacute;cembre 2009. Du moins pour les machines &eacute;quip&eacute;es du moteur AGO d'origine. Les machines Corail Intercit&eacute;s sont radi&eacute;es et le parc Fret est gar&eacute; bon &eacute;tat ou en r&eacute;paration diff&eacute;r&eacute;e. Six engins (03, 09, 18, 20, 27 et 85) poursuivent une seconde carri&egrave;re au Maroc.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alsthom, SACM<br />
Cha&icirc;ne de traction: Alternateur triphasé, redresseur au silicium, 
moteurs de traction à collecteurs et double réduction mécanique<br />
<br />
PV: 85 km/h,<br />
GV: 140 km/h sur les CC 72001 à 72020 sauf 72017<br />
160 km/h sur les CC 72017 et 72021 à 72092<br />
<br />
Motorisation: moteur di&eacute;sel SACM AGO V16 ESHR turbocompressé par 
deux turbo-soufflantes Hispano-Suiza HS 419 Bx2<br />
Bogies Y 218 monomoteurs, moteurs continus TAO 656 B1<br />
Puissance nominale: moteur thermique de 2650 kW utilis&eacute; &agrave; 
2320 kW, 2250 kW &agrave; la jante.</p>
<p>Motorisation CC 72075: SEMT Pielstick 16 PA 4-200 VGA<br />
Puissance nominale: 2350 kW</p>
<p> Longueur: 20,19 m<br />
Masse: 112t (72001 à 72020), 114t (72021 à 72092),<br />
118t pour la 72075 jusqu'en 1985,<br />
105t pour la 72075 &agrave; partir de 1987 et la 72044 de 1987 &agrave; 1997.</p>
<p>Dispositions particuli&egrave;res: les locs 72061, 62 et 64 sont modifiées 
pour le TGV Vendée</p>
<p class="titre2">Machines remarquables de la s&eacute;rie</p>
<p>La 72075 est sortie d'usine en 1973 &eacute;quip&eacute;e &agrave; titre 
d'essai d'un moteur SEMT 12 PA 6-280 utilis&eacute; &agrave; 3100 kW. 
Le surpoids des nouveaux &eacute;quipements portait la masse &agrave; 
118t. Ce moteur plus lent que les AGO V16 fut acc&eacute;l&eacute;r&eacute; 
pour atteindre 3530 kW lors de la seconde phase des essais men&eacute;e 
&agrave; partir de 1975. Malgr&eacute; les r&eacute;sultats satisfaisants 
de ces essais, les chocs p&eacute;troliers auraient pench&eacute; la balance 
en faveur de programmes d'&eacute;lectrifications intensives. Son moteur 
fut d&eacute;pos&eacute; en 1985.</p>
<p>En 1987, la 72044 ainsi que la 72075, d&eacute;motoris&eacute;e depuis 
deux ans, sont choisies pour mettre &agrave; l'essai un nouveau moteur. 
Le SEMT Pielstick 16 PA 4-200 VGA cousin du moteur qui &eacute;quipe quelques 
<a href="67400.php" class="link1">BB 67400</a>. D'une puissance l&eacute;g&egrave;rement 
inf&eacute;rieure au V16 AGO, il se veut moins polluant et doit en th&eacute;orie 
&eacute;quiper une hypoth&eacute;tique 73000. La 72044 cessera de rouler 
en 1997 pour cause de panne irr&eacute;parable par manque de pi&egrave;ces. 
La 72075 subira le m&ecirc;me sort en l'an 2000 alors que la 72044 ressort 
&eacute;quip&eacute;e d'un AGO classique. En 2004, la CC 72075 a b&eacute;n&eacute;fici&eacute; 
du programme de transformation des <a href="72100.php" class="link1">CC 
72100</a> en devenant la CC 72175.</p>
<p>La 72006 a re&ccedil;u en 1995 la livr&eacute;e Multiservice prototype 
&agrave; base de bleu. Toutefois cette livr&eacute;e appr&eacute;ci&eacute;e 
par les amateurs ne fut pas retenue. La machine appartenant &agrave; Fret 
fut repeinte dans cette livr&eacute;e.</p>
<p>La 72045 aura pass&eacute; pr&egrave;s d'un an avec une livr&eacute;e 
bien particuli&egrave;re : grise m&eacute;tallis&eacute;e. Sortie de r&eacute;vision 
en m&ecirc;me temps que la 72148 en juillet 2002, elle n'a re&ccedil;u 
le pelliculage &quot;En Voyage&quot; que lors de sa transformation en 
<a href="72100.php" class="link1">CC 72100</a> en Juin 2003.</p>
<p>La 72084 conservera sa livr&eacute;e d'origine jusqu'&agrave; 
la fin de sa carri&egrave;re o&ugrave; elle est appel&eacute;e &agrave; 
rejoindre le mus&eacute;e de Mulhouse.</p>
<p>La 72064 est en cours de pr&eacute;servation par l'ARCET. Son moteur a pu &ecirc;tre red&eacute;marr&eacute; au printemps 2015.</p>
<p>Avant l'amortissement de la s&eacute;rie, 3 machines manquaient &agrave; l'inventaire. La CC 72046 a 
heurt&eacute; un camion sur un PN en 1982. La cadette 72092, achemin&eacute;e 
en v&eacute;hicule alors que ses r&eacute;ducteurs &eacute;taient rest&eacute;s 
sur r&eacute;gime PV, prit feu tandis que l'ain&eacute;e 72001 fut percut&eacute;e 
par un locotracteur. Elles furent radi&eacute;es respectivement en 2002 
et 2003.<br />
Les 72011 et 72012 ont &eacute;t&eacute; amorties durant l'ann&eacute;e 2004.</p>
