<p><span class="titre">BB 26000</span><br />
<span class="soustitre">Les Sybics</span></p>
<p>Les moteurs &agrave; courant alternatif sont connus depuis longtemps et appr&eacute;ci&eacute;s pour leur simplicit&eacute; et leur rendement. Cependant, l'application 
des moteurs &eacute;lectriques aux Chemins de Fer impose une utilisation en vitesse variable. Ils doivent donc &ecirc;tre aliment&eacute;s avec 
une fr&eacute;quence variable. Jusqu'au dernier quart du XXe si&egrave;cle, l'unique moyen pour y parvenir &eacute;tait le groupe tournant, 
compos&eacute; d'une g&eacute;n&eacute;ratrice et d'un alternateur m&eacute;caniques, lourd et peu fiable qui &eacute;quipait, par exemple, 
les <a href="/tr/14000.php" class="link1">CC 14000</a> &agrave; moteurs asynchrones. Faute de mieux, le moteur continu s&eacute;rie ou le 
moteur direct &eacute;taient donc syst&eacute;matiquement utilis&eacute;s malgr&eacute; leur talon d'achille : le collecteur. </p>
<p>L'av&egrave;nement des semi-conducteurs permettait enfin la r&eacute;alisation d'onduleurs &agrave; haute puissance avec, &agrave; la cl&eacute;, la 
qu&egrave;te de la machine universelle. La SNCF avait sollicit&eacute; Alsthom et MTE pour l'&eacute;tude de concepts asynchrone (<a href="15000.php" class="link1">BB 
10003</a>) et synchrone (<a href="15000.php" class="link1">BB 10004</a>). L'autopilotage du moteur synchrone (fr&eacute;quence de l'onduleur impos&eacute;e 
par la vitesse en sortie du moteur lui-m&ecirc;me) simplifiait le sch&eacute;ma &eacute;lectrique de cette solution. A l'inverse, l'extinction des thyristors 
de la solution asynchrone exigeait des circuits complexes. Aussi, la 10003 connaissait des soucis de fiabilit&eacute; et le poids sup&eacute;rieur 
des &eacute;quipements asynchrones posait probl&egrave;me pour r&eacute;aliser une locomotive bicourante. Telles furent les raisons qui pouss&egrave;rent 
la SNCF, contrairement aux compagnies germaniques, &agrave; choisir le moteur synchrone autopilot&eacute; pour sa nouvelle g&eacute;n&eacute;ration 
de TGV Atlantique et &agrave; poursuivre les essais sur deux <a href="/tr/22200.php" class="link1">BB 22200</a>, sorties d'usine sous les num&eacute;ros 20011 et 20012 et &eacute;quip&eacute;es 
d'une cha&icirc;ne de traction synchrone qui pr&eacute;figuraient les futures BB 26000. Leur conception Synchrone-Bicourant donna leur surnom aux 26000 : Les Sybics. </p>
<p>La 26000 fut la concr&eacute;tisation d'un vieux r&ecirc;ve de la traction ferroviaire. Un couple important &agrave; faible r&eacute;gime et la capacit&eacute; 
d'atteindre des vitesses &eacute;lev&eacute;es, la machine synchrone peut exploiter toute sa puissance sur toute la gamme des vitesses et rendre 
les 26000 universelles. L'ironie du sort voudra que, d&egrave;s lors que la technique permettait de telles machines, la nouvelle donne &eacute;conomique 
d&eacute;cidera de rebrousser chemin vers des parcs sp&eacute;cialis&eacute;s.</p>
<p> Livr&eacute;es en 1988, les BB 26001 &agrave; 26005 ont subi de longs essais avant d'&ecirc;tre suivies par 229 exemplaires mis en service de 
1990 &agrave; 1998. Les 26000 ont v&eacute;cu de nombreux d&eacute;boires de jeunesse mais se sont fiabilis&eacute;es au fil des ans. Leur polyvalence 
les a amen&eacute;es &agrave; chasser les anciennes s&eacute;ries de locomotives tant sur le Fret que des relations &agrave; 200 km/h. La r&eacute;gularisation 
de la cat&eacute;naire Midi &agrave; Toulouse leur permet de d&eacute;poss&eacute;der les <a href="6500.php" class="link1">CC 6500</a> de leur derni&egrave;res 
relations prestigieuses. De m&ecirc;me, elles roulent &agrave; 200 km/h en plaine d'Alsace sur des TER 200, d'Orl&eacute;ans au Croisic avec les 
Interloire ou de Paris &agrave; Cherbourg. Leurs roulements les emm&egrave;nent &eacute;galement sur la ligne du Bourbonnais, la ligne de la Bresse, la 
transversale sud et de nombreux mouvements Fret du nord au sud du pays.</p>
<p> Afin de proposer une solution asynchrone &agrave; son catalogue, Alstom a convenu avec la SNCF de transformer la commande de la derni&egrave;re 
tranche de Sybics en <a href="/tr/36000.php" class="link1">BB 36000</a> Astride tritension, et &eacute;quip&eacute;es de moteurs asynchrones et d'une chaine de traction &agrave; thyristors GTO.</p>
<p class="titre2">Mi-vie : la valse des num&eacute;ros</p>
<p>Les <a href="/tr/15000.php" class="link1">BB 15000</a> sont les derni&egrave;res machines &agrave; subir le cycles des &quot;r&eacute;visions 
g&eacute;n&eacute;rales&quot;. D&eacute;sormais, tous les organes des locomotives modernes suivent leur propres cycle de r&eacute;vision. Afin de 
r&eacute;duire au minimum le temps d'immobilisation des machines en atelier, les caisses des BB 26000 sont consid&eacute;r&eacute;es comme des 
organes ind&eacute;pendants. Apr&egrave;s r&eacute;vision, le num&eacute;ro d'une machine suit le bloc central d'appareillage tandis que la caisse change de machine et de num&eacute;ro. </p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: GEC Alsthom - MTE<br />
Tension d'alimentation: continu 1,5 kV et monophas&eacute; 25 kV, 50 Hz<br />
Cha&icirc;ne de traction: Hacheur/Pont redresseur, Onduleur synchrone<br />
Motorisation: 1 moteur par bogie<br />
Puissance totale: 5600 kW</p>
<p>Longueur: 17,710 m<br />
Masse: 90t<br />
Pantographes: 1 AL, 1 AM 90 U</p>
<p>Dispositions particuli&egrave;res: <br />
Troisi&egrave;me phare de la 26188 &agrave; la 26234.<br />
22 machines sont &eacute;quip&eacute;es du Memor II leur permettant de rouler au Luxembourg. Parmi elles, les 14 machines TER Alsace 
sont &eacute;quip&eacute;es en multiplexage pour pousser en r&eacute;versibilit&eacute; &agrave; 200 km/h les rames Corail TER200 Alsace.</p>

<p class="titre2">Machines remarquables de la s&eacute;rie</p>
<p>La 26070 a tourn&eacute;e dans une publicit&eacute; pour le TER Alsace. Elle re&ccedil;ut &agrave; cette occasion une livr&eacute;e sp&eacute;cifique. </p>
<p>La 26008 fut la premi&egrave;re machine portant la livr&eacute;e "En Voyage". Elle est d&eacute;sormais grise.</p>
<p>Suite au probl&egrave;me des droits d'image de la livr&eacute;e &quot;En Voyage&quot;, la 26046  fit l'objet d'essais d'une nouvelle livr&eacute;e 
&quot;Carmillon&quot;. La livr&eacute;e est &agrave; pr&eacute;sent appliqu&eacute;e au gr&eacute; des op&eacute;rations caisse. </p>
