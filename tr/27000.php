<p><span class="titre">BB 27000</span><br />
<span class="soustitre">Les Prima</span></p>
<p>La &quot;Locomotives Fret&quot; alias Prima de chez Alstom.</p>
<p>Machines limit&eacute;es &agrave; la conception simplifi&eacute;e pour r&eacute;duire au maximum l'entretien courant. Moteur asynchrones command&eacute;es par des onduleurs &agrave; transistors IGBT. Moteurs suspendus par le nez, solution r&eacute;duisant la vitesse limite &agrave; 140 km/h. Les machines sont d&eacute;pourvues de ligne de chauffage train.</p>
<p>180 exemplaires livr&eacute;s de 2001 &agrave; 2006. Certains exemplaires appartiennent &agrave; Akiem, filiale de la SNCF qui les loue &agrave; des entreprises ferroviaires priv&eacute;es. Ces 27000 roulent aux couleurs d'ECR ou de VFLI.</p>
<p>Les 27000 ont &eacute;t&eacute; d&eacute;clin&eacute;es en version tritension <a href="/tr/37000.php" class="link1">37000</a> et en version Banlieue <a href="/tr/27300.php" class="link1">27300</a>. </p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom<br />
Tension d'alimentation: continu 1,5 kV et monophas&eacute; 25 kV, 50 Hz<br />
Cha&icirc;ne de traction: onduleurs &agrave; transistors IGBT - Moteur asynchrones<br />
Motorisation: 2 moteurs suspendus par le nez par bogie<br />
Puissance totale: 4200 kW<br />
Aptitude &agrave; l'UM</p>
<p>Longueur: 19,520 m<br />
Masse: 90t<br />
Pantographes: 2</p>
  
