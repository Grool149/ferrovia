  <tr> 
    <td valign="top">
      <p class="text">Les Z 5300 sont des &eacute;l&eacute;ments automoteurs monocourants 1,5 kV continu quadricaisses construits en inox selon le proc&eacute;d&eacute; de soudure Budd. Une motrice &agrave; adh&eacute;rence totale et une voiture pilote encadrent deux remorques. </p>
      <p class="text">Elles ont servi en banlieue parisienne sur les banlieues Sud-Est, Montparnasse et Sud-Ouest, puis &agrave; la cr&eacute;ation du RER, sur la ligne C o&ugrave; elles desservaient notamment la ligne de ceinture entre Juvisy, Massy et Versailles Chantiers.</p>
      <p class="text">En r&eacute;gion, elles ont assur&eacute; au d&eacute;but de leur carri&egrave;re des express Paris-Orl&eacute;ans, et jusqu'&agrave; plus r&eacute;cemment des Paris-Chartres-Le Mans d'o&ugrave; elles ont &eacute;t&eacute; relev&eacute;es par des <a href="/tr/z24500-26500.php" class="link1">Z 26500</a>.</p>
      <p class="text">La hausse de la fr&eacute;quentation en banlieue parisienne et l'inconfort de ces rames ont pouss&eacute; la SNCF &agrave; les remplacer par des rames &agrave; deux niveaux. Elles ont ainsi &eacute;t&eacute; chass&eacute;es du RER C, n'assurant plus que les trains de grande banlieue au-del&agrave; de Melun sur le r&eacute;seau Sud-Est. Il n'y a plus qu'&agrave; Montparnasse o&ugrave; elles irriguent encore la petite couronne sur des omnibus entre Paris-Montparnasse et S&egrave;vres notamment. Les rames lib&eacute;r&eacute;es sont all&eacute;es r&eacute;aliser des navettes entre les gares de Tours et St Pierre des Corps, ainsi qu'entre Orl&eacute;ans et les Aubrais. Leur composition &eacute;tait alors r&eacute;duite &agrave; trois caisses. </p>
      <p class="text">Un autre lot est parti en Aquitaine o&ugrave; elles &eacute;taient engag&eacute;es en TER autour de Dax.  <br />
      Avec la livraison des automoteurs TER modernes, toutes les rames r&eacute;gionales ont &eacute;t&eacute; radi&eacute;es. La purge d&eacute;finitive des unit&eacute;s parisiennes est en cours. La livraison des NAT Z 50000 va lib&eacute;rer des VB2N qui, associ&eacute;es &agrave; des <a href="/tr/7200.php" class="link1">BB 7200</a> transform&eacute;es en BB 7600, vont &ecirc;tre d&eacute;ploy&eacute;es sur le r&eacute;seau Montparnasse. </p>
