<p><span class="titre">BB 25100</span></p>
      <p>Les BB 25100 furent les premi&egrave;res machines Jacquemin 
        bicourant d&eacute;rivant de la <a href="20005.php" class="link1">BB 20005</a>. 
        Limitées à 130 km/h, elles étaient destinées à assurer le transport de 
        marchandises entre les régions industrielles du Bénélux, du Nord et de 
        la Lorraine et Dijon, important noeud &eacute;lectrifi&eacute; en 1,5 
        kV. La SNCF les réceptionna de Juillet 1964 à Juin 1965. Apr&egrave;s 
        quoi les usines du Creusot enchain&egrave;rent la production des <a href="25200.php" class="link1">BB 
        25200</a>. Alors que les dix premiers exemplaires &eacute;taient livr&eacute;s, 
        l'électrification de la ligne Chalindrey-Dijon n'était pas achevée. Ils 
        furent donc envoyés à Dôle et assurèrent des trains internationaux entre 
        Paris et Vallorbe sans relais.</p>
      <p> Après ce bref épisode, les BB 25100 furent mutées à Chalindrey 
        où les livraisons ont continu&eacute;. Leur service Nord-Sud ne connaitra 
        alors que peu de variations au cours de leur carrière. Avec l'explosion 
        du trafic marchandises, des Trains-Auto-Couchettes et des trains d'agence 
        entre l'Europe du Nord et la Méditerranée, les 25 BB 25100 &eacute;taient 
        devenues insuffisamment nombreuses. La SNCF commanda donc 20 locomotives 
        <a href="25150.php" class="link1">BB 25150</a> quasi-identiques d&egrave;s 
        1965.</p>
      <p> Elles ont sillonnées le Nord-Est et descendaient jusqu'Ambérieu. 
        L'arrivée des machines modernes et surtout des <a href="/tr/27000.php" class="link1">BB 27000</a> les a poussé à 
        la retraite. L'amortissement débuté en 2002 s'est intensifié en 2005 et 
        seules 9 machines restaient à l'inventaire début 2006. Les deux derni&egrave;res furent radi&eacute;es en 2007 peu apr&egrave;s leur mutation &agrave; Rennes et revendues &agrave; l'op&eacute;rateur priv&eacute; Roumain GFR. </p>
      <p class="titre2">Les livr&eacute;es</p>
      <p>La livrée d'origine était identique à celle des <a href="9200.php" class="link1">BB 
        9200</a> et <a href="16000.php" class="link1">BB 16000</a> avec des fanaux 
        rouges d'origine.</p>
      <p> Le béton a touché la série avec des variations sur les teintes, la forme 
        des moustaches ou des marquages.</p>
      <p> La livrée Multiservice ne fut appliquée que sur la BB 25120</p>
      <p> La livrée Fret fut appliquée sur les 25109, 14, 21 et 22.</p>

<p class="titre2">Quelques donn&eacute;es techniques</p>
      <p>Constructeur: MTE Jeumont Schneider - Société des Forges 
        et Ateliers du Creusot.<br />
        Tension d'alimentation: 1,5 kV continu, monophasé 25 kV 50 Hz<br />
        Chaîne de traction 1,5 kV: Banc rhéostatique commandé par graduateur<br />
        Chaîne de traction 25 kV: Graduateur HT, redresseurs silicium.<br />
        Motorisation: 2 moteurs par bogie<br />
        Puissance totale: 25 kV : 4130 kW ; 1,5 kV : 3400 kW</p>
      <p> Longueur: 16,2 m</p>
      <p> Masse: 84t<br />
        Pantographes: AM 18 B et AM 184</p>
      <p> Vitesse maximum autorisée : 130 km/h</p>
