<div class="Section_txt">
<p>L'&eacute;radication de la traction vapeur passait par de grands chantiers d'&eacute;lectrification. Mais bon nombre de lignes ne pouvaient b&eacute;n&eacute;ficier
de si co&ucirc;teux travaux. C'est le point de d&eacute;part de la di&eacute;s&eacute;lisation massive du parc de la SNCF.</p>
<p>Les &eacute;tudes men&eacute;es par les compagnies pendant la premi&egrave;re moiti&eacute; du vingti&egrave;me si&egrave;cle sur les engins thermiques restaient cependant d&eacute;cevantes.
Le contexte de la seconde guerre mondiale permit d'importants progr&egrave;s techniques et la SNCF entreprit les diff&eacute;rentes voies qui s'offraient &agrave; elle.</p>
<p>Comme en traction &eacute;lectrique, l'essor des technologies fut tr&egrave;s rapide &agrave; partir des ann&eacute;es 50. Cette d&eacute;c&eacute;nie vit des projets
aux r&eacute;sultats peu satisfaisants &agrave; cause de la puissance limit&eacute;e des moteurs qui imposait des sch&eacute;mas de machines lourdes &agrave; 6 essieux et bimoteurs.
Ce qui impliquait des soucis de synchronisation, un co&ucirc;t &eacute;lev&eacute; de maintenance et une fiabilit&eacute; insuffisante. Les ann&eacute;es 60 virent les progr&egrave;s
les plus significatifs avec l'arriv&eacute;e des moteurs industriels de grande puissance.</p>
<p>Mais, contrairement &agrave; la traction &eacute;lectrique, cet age d'or s'est achev&eacute; sans suite en 1969. Ainsi la derni&egrave;re machine 
di&eacute;sel neuve acquise par la SNCF fut longtemps la BB 67632, mise en service en 1975. Les nouveaut&eacute;s se limitant aux automoteurs et &agrave; la 
location de machines Vossloh.</p>
<p>L'avenir du parc di&eacute;sel semble aujourd'hui limit&eacute; au fret avec la commande des BB 4-75000 &agrave; moteur MTU et cha&icirc;ne de 
traction asynchrone  coproduites par Siemens et Alstom, et une main-mise des automoteurs sur les services voyageurs. En attendant ces prochains bouleversements, 
le parc actuel bat des records de long&eacute;vit&eacute;s gr&acirc;ce &agrave; des op&eacute;rations de r&eacute;novation.</p>
