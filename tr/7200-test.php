    <td valign="top">
      <p class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> La g&eacute;n&egrave;se des hacheurs de courant</p>
      <p>Jusqu'au d&eacute;but des ann&eacute;es 1970, il n'y avait pas 36 fa&ccedil;ons de d&eacute;marrer 
        une locomotive &agrave; courant continu. Afin d'alimenter les moteurs avec une 
        tension faible, ils &eacute;taient branch&eacute;s en s&eacute;rie sur un pont de r&eacute;sistances appel&eacute; 
        le rh&eacute;ostat. Les r&eacute;sistances &eacute;taient &eacute;limin&eacute;es les unes apr&egrave;s les autres 
        &agrave; mesure que les moteurs acc&eacute;l&eacute;raient et que leur tension contre-&eacute;lectromotrice 
        augmentait. Ce syst&egrave;me pr&eacute;sente de lourds inconv&eacute;nients. Une telle chaine 
        de traction tire autant de courant &agrave; la cat&eacute;naire qu'il en passe par les 
        moteurs. Outre des probl&egrave;mes d'&eacute;chauffement des pantographes, de la cat&eacute;naire 
        et une chute de tension importante de celle-ci, la majorit&eacute; de la puissance 
        consomm&eacute;e au d&eacute;collage est dissip&eacute;e en chaleur par le rh&eacute;ostat. Non seulement 
        il s'agit d'un g&acirc;chis mais le rh&eacute;ostat peut s'&eacute;chauffer jusqu'&agrave; la fusion. 
        Ce qui rend son usage strictement r&eacute;serv&eacute; &agrave; la mise en vitesse de la locomotive 
        sur des laps de temps courts. La souplesse de conduite s'en ressent beaucoup.</p>
      <p>L'apport des semi-conducteurs dans les cha&icirc;nes de traction &agrave; courant continu 
        &eacute;tait donc tr&egrave;s prometteur. Mais les hacheurs de courant &eacute;taient &eacute;galement 
        d&eacute;licats &agrave; mettre au point. En raison des fr&eacute;quences harmoniques qu'ils 
        &eacute;mettaient dans la cat&eacute;naire et les rails, risquant de perturber la signalisation. 
        Ainsi les thyristors furent utilis&eacute;s d'abord sur des machines monophas&eacute;es, 
        les <a href="15000.php" class="link1">BB 15000</a> (c&acirc;blage en pont mixte).</p>
      <p>Les hacheurs furent exp&eacute;riment&eacute;s en conditions r&eacute;elles en 1971 sur la 
        CC 20002, ex-prototype bicourant de Savoie. Elle &eacute;tait d&eacute;motoris&eacute;e et 
        alimentait les moteurs de la <a href="9200.php" class="link1">BB 9252</a>. 
        Ces essais se poursuivirent par l'int&eacute;gration de l'&eacute;quipement dans une 
        machine unique. La <a href="15000.php" class="link1">BB 15007</a> fut 
        transform&eacute;e en BB 7003. </p>

      <p class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> Construction des BB 7200</p>
      <p>Les BB 7200 sont directement issues de la 7003. Elles reprennent la caisse et les bogies des <a href="15000.php" class="link1">BB 15000</a>. 
        Toutefois, les persiennes inox sont centr&eacute;es et la livr&eacute;e adopte les couleurs 
        b&eacute;ton. Les locomotives 7236 et suivantes ont b&eacute;n&eacute;fici&eacute; 
        d'un pare-brise moins inclin&eacute; offrant plus de place en cabine. 
        240 exemplaires ont &eacute;t&eacute; produits de 1976 &agrave; 1985. Les 7200 ont donn&eacute; naissance 
        aux <a href="/tr/22200.php" class="link1">BB 22200</a> bicourantes qui sont leur quasi-jumelles.</p>
      <p>Trois machines sont aptes au V200 (7261 &agrave; 63).
        <!--Ce sous parc &eacute;tait 
        jadis plus important mais ce sont des 22200 qui ont r&eacute;cup&eacute;r&eacute; 
        la plupart des bogies V200.-->
        Les 7292 &agrave; 7299 ont des tampons hydrauliques leur permettant de 
        tirer des rames Talgo III, sur le Catalan Talgo.</p>
      <p>Jusqu'en 2011, 100 machines disposaient d'un rapport de r&eacute;duction PV (102 m&ecirc;me en comptant les 7210 et 7348 victimes d'incendies respectivement en 2001 et 2006). 67 de ces 
        derni&egrave;res poss&egrave;dent &eacute;galement l'UM par multiplexage et les 30 derni&egrave;res le freinage par r&eacute;cup&eacute;ration, tr&egrave;s utile dans les pentes de la Maurienne. La purge des plus anciennes machines Fret a alors touch&eacute; la premi&egrave;re sous-s&eacute;rie de BB 7200 &agrave; petites cabines. Sur les 33 machines restantes, 12 ont &eacute;t&eacute; amorties et le reste transf&eacute;r&eacute; aux activit&eacute;s r&eacute;gionales. 20 jeux de bogies V100 ont &eacute;t&eacute; &eacute;chang&eacute;s pour prendre place sous des BB 7200 Fret grandes cabines.</p>
      <p>14 machines sont modifi&eacute;es  pour assurer des Transilien sur le r&eacute;seau Montparnasse. Elles sont &eacute;quip&eacute;es de la r&eacute;versibilit&eacute; par c&acirc;blots et des &eacute;quipements sp&eacute;cifiques &agrave; la banlieue (EAS, SIVE) dans le poste 1. Le poste 2, toujours attel&eacute; &agrave; la rame, ne sert que pour les &eacute;volutions et en secours. Elles sont renum&eacute;rot&eacute;es BB 7600 pour &eacute;viter toute confusion de num&eacute;ro avec les <a href="/tr/27300.php" class="link1">27300</a> qu'elles c&ocirc;toieront (il serait f&acirc;cheux d'envoyer une 7300 &agrave; Dreux sous 25 kV). L'id&eacute;e est de r&eacute;utiliser les VB2N de Paris-Nord remplac&eacute;es par des Z 50000, et de pousser les derni&egrave;res <a href="/tr/5300.php" class="link1">Z 5300</a> &agrave; la retraite. </p>
      <p class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> Les livr&eacute;es</p>
      <p>L'ensemble de la s&eacute;rie a re&ccedil;u la livr&eacute;e b&eacute;ton. 
        Mis &agrave; part quelques exceptions notables, la livr&eacute;e n'a pas 
        chang&eacute; hormis une &eacute;volution des teintes, des sigles SNCF, 
        ainsi que la d&eacute;pose des plaques en relief et le masquage des num&eacute;ros 
        frontaux.</p>
      <p>Les machines les plus r&eacute;centes (~7390 &agrave; 7440) sont les 
        derni&egrave;res &agrave; poss&eacute;der leurs livr&eacute;es d'origine 
        et leurs plaques.</p>
      <p>Jusqu'en 2005, peu d'engins d&eacute;rogaient &agrave; la livr&eacute;e b&eacute;ton. 
        Suite &agrave; des accidents ou des op&eacute;rations caisse, quelques 
        machines ont rev&ecirc;tu les livr&eacute;es Multiservice (7201, 221, 
        258, 292), Fret (7206, 219, 223, 242, 396, 400, 411) ou &quot;en voyage&quot; (7284).</p>
      <p> Depuis, la livr&eacute;e &quot;En voyage&quot; a &eacute;t&eacute; appliqu&eacute;e sur les machines CIC/TER passant en r&eacute;vision et recevant un &eacute;quipement de r&eacute;versibilit&eacute;. La BB 7321, affect&eacute;e &agrave; la r&eacute;gion Bourgogne en a re&ccedil;u la livr&eacute;e sp&eacute;cifique.</p>
      <p>En 2010, avec le coup d'arr&ecirc;t de cette livr&eacute;e pour des probl&egrave;mes de droits d'image, les 7216 et 7219 sont sorties d'atelier dans une livr&eacute;e &quot;fant&ocirc;me&quot; uniform&eacute;ment grise m&eacute;talis&eacute;e semblable &agrave; celle des premi&egrave;res machines &quot;En voyage&quot; jusqu'&agrave; l'application de leur pelliculage. </p>

