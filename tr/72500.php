  <tr> 
    <td valign="top">
      <p class="text">Destin&eacute;es &agrave; assurer des relations Autorail express longue distance et &agrave; r&eacute;hausser la qualit&eacute; du mat&eacute;riel thermique r&eacute;gional de la SNCF, les X 72500 sont un mat&eacute;riel r&eacute;volutionnaire &agrave; leur arriv&eacute;e en 1997. Ils sont la t&ecirc;te de pont d'une nouvelle g&eacute;n&eacute;ration d'autorails et plus largement de mat&eacute;riel r&eacute;gional. Nouveaux coloris &agrave; base de gris m&eacute;tallis&eacute; et bleu marine, si&egrave;ges individuels, climatisation, nez carr&eacute;n&eacute;s et performances &eacute;lev&eacute;es (vitesse limite de 160 km/h, une premi&egrave;re) leur valent un surnom de petit TGV par les clients.</p>
      <p class="text">La motorisation des XTER est fortement dimensionn&eacute;e. Chaque motrice emporte deux moteurs 6 cylindres MAN de 300 kW qui engr&egrave;nent chacun sur un essieu. La puissance des auxiliaires est fournie par un groupe &eacute;lectrog&egrave;ne ind&eacute;pendent dans chaque caisse. Comme l'XTER s'att&egrave;le par Sharffenberg, il ne peut s'associer aux XR 6000 &agrave; 6200 existantes. Les rames d'un ou de plusieurs X 72500 en UM conservent alors un rapport poids puissance des plus &eacute;lev&eacute;s.</p>
      <p class="text">Les XTER s'emparent de Paris-Vend&ocirc;me-Tours puis de Paris-Granville. Sur cet axe, il ne s'agit pas de TER mais d'un service intercit&eacute;. Les quinze X 72500 affect&eacute;s sur cette ligne re&ccedil;oivent une remorque interm&eacute;diaire, laquelle poss&egrave;de &eacute;galement son propre groupe auxiliaire. Ces rames ne portent pas le grand autocollant TER qui orne le reste de la s&eacute;rie. </p>
      <p class="text">Les services des XTER se sont &eacute;tendus &eacute;galement sur Marseille-Brian&ccedil;on, Valence-Grenoble-Chamb&eacute;ry-Gen&egrave;ve, Mende-N&icirc;mes-Montpellier,  Clermont-Ferrand-Toulouse, Paris-Culmont, Tours-Caen, Paris-Laon... Une commande suppl&eacute;mentaire porte le nombre de rames de 105 &agrave; 117. 17 remorques sont &eacute;galement command&eacute;es. Les nouvelles rames sont livr&eacute;es en 2002.</p>
      <p class="text">H&eacute;las, les X 72500 r&eacute;v&egrave;lent tr&egrave;s vite des d&eacute;fauts de jeunesse qui perdureront. Soucis de transmission, de groupes auxiliaires, des portes d'acc&egrave;s, fragilit&eacute; des nez amovibles, une r&eacute;putation de b&ecirc;te &agrave; chagrin leur colle depuis &agrave; la peau. Le nombre de moteurs rend l'engin particuli&egrave;rement bruyant et le confort n'&eacute;galera jamais celui d'une rame tract&eacute;e.</p>
      <p class="text">Tous ces soucis  les ont &eacute;vinc&eacute;s de plusieurs relations au profit des AGC Bombardier plus fiables et silencieux. <br />
        A la mise &agrave; la retraite des RTG, la relation Lyon-Bordeaux est confi&eacute;e quelques temps &agrave; des rames Corail qui perdent beaucoup de temps aux nombreux rebroussements et qui subissent des ralentissements sur les grands viaducs d'Auvergne. Le service est alors confi&eacute; aux X 72500. Ils reprennent aussi les Lyon-Nantes par Moulins apr&egrave;s le retrait des <a href="/tr/72000.php" class="link1">CC 72000</a>.</p>
      <p class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> Machines remarquables de la s&eacute;rie</p>
      <p class="text">l'X 72547/48 a particip&eacute; &agrave; des essais de suspension pendulaire en tant que d&eacute;monstrateur Alstom. Le projet n'a pas eu de suite et il fut remis au type. </p>
      <p class="titre" style="font-size:14px"><img src="../images/puce.gif" alt="puce" width="12" height="12" /> Quelques donn&eacute;es techniques</p>
<p class="text">Constructeur : Alstom <br />
        Motorisation : 2 moteurs 6 cylindres MAN turbocompress&eacute;s par motrice, 4 par rames. <br />
        Transmission : Boite de vitesse hydraudynamique Voith.<br />
  Puissance totale: 1200 kW (sans compter les auxiliaires) <br />
  Aptitude &agrave; l'UM entre eux </p>
<p class="text">  Longueur: 52,90 m (bicaisse), 78,50 m (tricaisse)<br />
  Masse: 116t (bicaisse), 161t (tricaisse)    </p>
  
