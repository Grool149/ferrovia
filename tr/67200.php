<p class="titre">BB 67200</p>
<p>Les <a class="link1" href="67000.php">BB 67000</a> sont cantonn&eacute;es 
au service Fret &agrave; partir de 1975. Les ann&eacute;es qui suivent 
voient un d&eacute;clin des besoins et les programmes d'&eacute;lectrifications 
all&egrave;gent leur charge de travail. Parall&egrave;lement, la cr&eacute;ation 
des lignes &agrave; grande vitesse cr&eacute;e de nouveaux besoins : l'entretien 
des lignes et le secours des TGV n&eacute;cessitent des machines autonomes 
et &eacute;quip&eacute;es de la TVM.</p>
<p>Les <a class="link1" href="67000.php">BB 67000</a> sont donc modifi&eacute;es 
en BB 67200 &agrave; partir de 1980 en plusieurs tranches suivant les 
besoins grandissants du r&eacute;seau TGV qui s'&eacute;tend.</p>
<p>Pas moins de 72 machines ont b&eacute;n&eacute;fici&eacute; de cette 
cure de jouvence s'accompagnant de nouveaux &eacute;quipements : TVM, 
Radio Sol train et attelage Scharffenberg repliable pour certaines machines.</p>
<p>Le tr&egrave;s faible kilom&eacute;trage mensuel r&eacute;alis&eacute; 
par ces machines, qui souvent attendent qu'on ait besoin d'elles, leur 
promet un avenir encore long.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Brissonneau et Lotz<br />
Cha&icirc;ne de traction: G&eacute;n&eacute;ratrice de courant continu, 
moteurs de traction &agrave; collecteurs et double r&eacute;duction m&eacute;canique 
bloqué en PV (90 km/h)<br />
Motorisation: moteur di&eacute;sel SEMT-Pielstick 16 PA 4-185<br />
1 moteur de traction SW 9209 enti&egrave;rement suspendu par bogie<br />
Puissance totale: 1470 kW (moteur thermique), 1240 kW &agrave; la jante.<br />
<br />
Longueur: 17,090 m<br />
Masse: 80,725t<br />
Aptitude &agrave; l'UM avec les BB 67000, A1A-A1A 68000 et 68500.</p>
<p>Dispositions particuli&egrave;res: les locs 67219, 220, 250 et 251 disposent 
de connecteurs leur permettant de communiquer avec des voitures de mesure 
&agrave; l'instar des BB 622200 TVM. </p>
<p class="titre2">Machine remarquable de la s&eacute;rie</p>
<p> La 67210 a rev&ecirc;tu &agrave; titre d'essai la livr&eacute;e Infrastructure 
au printemps 2002. Cette livr&eacute;e bas&eacute;e sur la livr&eacute;e 
Fret ne semble cependant pas avoir de suite. Pourtant l'&eacute;tat de 
nombreuses BB 67200 r&eacute;clame bien un petit coup de pinceau...</p>
