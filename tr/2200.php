<p><span class="titre">X 2200</span></p>
<p>Autorails monocaisses directement d&eacute;riv&eacute;s des <a href="/tr/2100.php" class="link1">X 2100</a>, mais dont la construction est simplifi&eacute;e pour en diminuer le co&ucirc;t.</p>
<p>Motorisation sous caisse par un 6 cylindres Di&eacute;sel en ligne Saurer de 600ch. Ces autorails sont aptes &agrave; l'UM avec les <a href="/tr/2100.php" class="link1">X 2100</a>, <a href="/tr/2800.php" class="link1">X 2800</a> et forment des rames avec les XR 6000, 6100 et 6200.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur : ANF<br />
Motorisation : L6 Saurer S1 DHR turbocompress&eacute;<br />
Transmission :  convertisseur hydraulique Voith.<br />
Puissance totale: 440 kW<br />
Vitesse limite: 140 km/h<br />
Aptitude &agrave; l'UM</p>
<p>Longueur: 22,40 m<br />
Masse: 43t</p>
