      <p> Rustiques et économiques, c'est ainsi que l'on peut qualifier les Caravelles. 
        Ces rames automoteurs diésel tirent leur surnom de la concommitance de 
        leur sortie d'usine avec celle du premier avion de ligne à réaction Français 
        en 1963. Leur nom officiel, EAD, évoque davantage leur nature : Eléments 
        Automoteurs Doubles (on lit parfois aussi Eléments Automoteurs Diésel). 
        Plus rare, l'appelation U425 signifie, de même que pour les autres autorails 
        unifiés, qu'ils sont équipés d'un moteur de 425 ch, ce qui n'est pas vrai 
        pour toutes les séries. </p>
      <p>Le concept novateur de ces rames tenait en deux points : Composition 
        indéformable à deux caisses réversible, supprimant les manoeuvres d'une 
        remorque. Et implantation du moteur sous caisse permettant de loger davantage 
        de sièges. Cette dernière disposition avait été appliqué en premier sur 
        l'automoteur prototype Budd X 2051, et fut reconduite avec les X 2100 
        et les X 2200. La motorisation modeste des EAD, leur confort précaire 
        et leur allure peu flatteuse les ont cependant laissé dans l'ombre d'autorails 
        plus illustres tels que les RGP1 ou les X2800. Leur conception rudimentaire 
        a même poussé les conseils régionnaux à entreprendre de profondes modernisations 
        améliorant nettement le confort et la rigidité des cabines. </p>
      <p>La construction des Caravelles s'est étalée sur 18 ans pour un total 
        impressionnant de 456 rames toutes assemblées par les ANF dans l'usine 
        de Crespin-Blanc-misseron d'où sortent aujourd'hui les AGC, leur dignes 
        successeurs, qui les poussent massivement à la retraite. Au cours de ces 
        deux décennies, plusieurs modifications importantes sont intervenues conduisant 
        à la création de 5 séries des rames reposant sur 4 séries de motrices 
        et 4 séries de remorques. La numérotation des EAD est donc relativement 
        complexe et atypique. </p>
      <p>Dès 1963, deux séries indépendantes sont produites en même temps. Les 
        X 4300 et 4500 se distinguent par leurs motorisations issues de deux constructeurs 
        différents. Les X 4630, semblables aux X4500, sont mis en service à partir 
        de 1971. La nouveauté est l'échange de la boite de vitesse mécanique pour 
        une boite hydraulique. Arrivent ensuite les X 4900, appelés EAT pour Eléments 
        Automoteurs Triples, qui sont des rames tricaisses surmotorisées. Elles 
        sont composées de deux motrices de type X 4630 encadrant une remorque 
        sans cabine. Les X 4750 sont enfin les plus puissants des rames doubles 
        avec un nouveau moteur de 440 kW (600 ch). Ils ont commencé leur carrière 
        en 1977 et furent déclinés sous forme de rames postales et d'autorails 
        express pour la Basse-Normandie. </p>
      <p>A noter aussi que les Caravelles ont donné naissance aux turbotrains 
        avec le prototype TGS et possédent de nombreux points communs avec les 
        ETG. </p>
