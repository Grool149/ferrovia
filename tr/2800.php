<p><span class="titre">X 2800 - U825</span><br />
<span class="titre2">Le Bleu d'Auvergne</span>
</p>
<p>La construction d'autorails puissants avait recours &agrave; l'emploi de plusieurs moteurs de 300 chevaux. Ce qui posait des soucis de synchronisation, un entretien assez co&ucirc;teux et un encombrement diminuant d'autant le nombre de places &agrave; bord. La mise au point de moteurs rapides et puissants permit de d&eacute;velopper des autorails monomoteurs de 825ch.</p>
<p>L'X 2800 est donc un engin fortement d&eacute;riv&eacute; de l'<a href="/tr/2400.php" class="link1">X 2400</a> et troque les deux V12 renault 517 de 300ch contre un unique V12 MGO de 825ch. Comme son pr&eacute;d&eacute;cesseur il a un &eacute;quivalent bi-caisse : Les <a href="/tr/2700.php" class="link1">RGP 1</a>.</p>
<p>Construits &agrave; 119 exemplaires, les 16 premiers engins sortent de l'usine Decauville de Corbeil. L'usine Renault de Choisy-le-roi fabriqua le reste. </p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur : Decauville puis Renault<br />
Motorisation : V12 MGO turbocompress&eacute;, &eacute;chappement libre<br />
Transmission : Boite de vitesse automatique Maybach Mekydro &agrave; 4 rapports et convertisseur hydraulique.<br />
Puissance totale : 825ch<br />
Aptitude &agrave; l'UM avec les <a href="/tr/2100.php" class="link1">X 2100</a>, <a href="/tr/2200.php" class="link1">2200</a> et eux-m&ecirc;mes (2 engins moteur maxi)</p>
<p>Vitesse max : 120 km/h, port&eacute;e &agrave; 140 km/h sur certains engins reconnaissables &agrave; leur dispositif lave-glace. </p>
<p>Longueur: 27,730 m<br />
Masse: 54t</p>
