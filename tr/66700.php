<p class="titre">BB 66700</p>
<p>Locomotives di&eacute;sel-&eacute;lectriques de moyenne puissance monocabine adapt&eacute;es &agrave; la manoeuvre. Les BB 66700 sont des machines transform&eacute;es &agrave; partir de <a href="/tr/66000.php" class="link1">BB 66000</a> pour la pousse &agrave; la butte. Elles se distinguent par  des vitres suppl&eacute;mentaires afin d'am&eacute;liorer la visibilit&eacute; en manoeuvre et une livr&eacute;e orange proche de celle des <a href="/tr/63000.php" class="link1">BB 63500</a> pour les 24 premi&egrave;res locomotives transform&eacute;es entre 1985 et 1991. </p>
<p>Une seconde s&eacute;rie de dix machines sont transform&eacute;es en 2003. Elles portent la livr&eacute;e Fret.</p>
