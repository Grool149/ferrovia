<p><span class="titre">Corail TER</span><br />
<span class="soustitre">Les nouvelles livrées TER</span></p>
<p>Quelques régions ont décidé de pérenniser leurs Corail TER en les rénovant. Elles en profitent pour s'écarter de la livrée TER standard en ajoutant une touche personnelle (il faut bien rappeler qui a payé la facture).</p>
<p class="titre2">TER Alsace.</p>
<p>Cela fait bientôt 25 ans que TER Alsace possède des Corail. A l'occasion de leur raffraîchissement, elles ont reçu la livrée standard TER. Cependant, un pelliculage important leur fut appliqué. Ces rames circulent désormais en réversibilité à 200 km/h</p>
