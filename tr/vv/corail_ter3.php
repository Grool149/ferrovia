<p class="titre2">TER Bourgogne</p>
<p>Bourgogne était préssentie pour commander des automotrices Régio2N pour remplacer ses rames Corail sur l'axe Paris-Laroche-Dijon. La région a préféré investir dans la transformation de voitures pilotes B5uxh et rénover
intégralement ses Corail. De quoi attendre une bonne décénie.</p>
