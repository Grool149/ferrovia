    <td valign="top"><span class="text"> 
      <p>Parall&egrave;lement &agrave; son grand programme de voitures unifi&eacute;es 
        pour renouveler un parc obsol&egrave;te, la DEV entreprit l'&eacute;tude 
        de voitures en acier inoxydable. Ce mat&eacute;riau permettait un gain 
        de poids appr&eacute;ciable et l'absence de peinture. Il &eacute;tait 
        d&eacute;j&agrave; utilis&eacute; sur les Z 3700 du r&eacute;seau de l'ex-ETAT 
        construites par Carel et Fouch&eacute; qui poss&eacute;dait la licence 
        sur les techniques de fabrication brevet&eacute;es par Budd. A l'&eacute;poque, 
        en pleine p&eacute;riode de reconstruction, l'Inox &eacute;voquait la 
        technologie Am&eacute;ricaine et jouissait d'une image de luxe.</p>
      <p>Les DEV Inox &eacute;taient des voitures de haut standing, destin&eacute;es 
        &agrave; entrer dans la composition des trains les plus nobles, c'est 
        &agrave; dire les plus rapides. La construction de cette s&eacute;rie 
        s'est &eacute;tal&eacute;e sur vingt ans avec des diff&eacute;rences tr&egrave;s 
        marqu&eacute;es puisque, &agrave; l'instar des DEV AO, la longueur du 
        chassis fut modifi&eacute;e. Les voitures des programmes de construction 
        U50 &agrave; U54 (+ 2 voitures U55) faisaient 23,344 m de long pour une 
        largeur de 2,925 m. Tandis que les voitures U55 &agrave; U70 faisaient 
        25,094 m de long et 2,890 m de large.</p>
      <p> En pr&eacute;vision de la suppression de la 3&egrave; classe en 1956, 
        les voitures U50 et U52 poss&eacute;daient des compartiments de 1e ou 
        2e classe de dimension identique. Les premi&egrave;res voitures &eacute;quip&egrave;rent 
        le plus renomm&eacute; des trains de l'entre deux guerres : la Fl&egrave;che 
        d'Or, puis les rapides Paris-Strasbourg et le &quot;Drapeau&quot; Paris-Bordeaux. 
        Alors que les bogies Y16 prenaient encore place sous les voitures de 2e 
        et mixte U50, les bogies Y20 de type &quot;Milwaukee&quot; se g&eacute;n&eacute;ralis&egrave;rent. 
        Le programme U52 &eacute;quipa principalement le &quot;Mistral&quot; qui 
        devenait Le train prestigieux de la SNCF. Parmi ce lot, deux voitures 
        bar A5smyfi poss&eacute;dait une climatisation r&eacute;volutionnaire 
        pour l'&eacute;poque. Aliment&eacute;e par une g&eacute;n&eacute;ratrice, 
        elle apportait cependant un surpoids non n&eacute;gligeable. La rame d&eacute;finitive 
        du Mistral &eacute;tait pr&eacute;vue au programme U53. Ces voitures climatis&eacute;es 
        firent appel &agrave; une autre solution moins contraignante. Un fourgon 
        ex-PO alimentait la rame par une ligne de train sp&eacute;cifique en 600V. 
        Livr&eacute; en 1956, ce mat&eacute;riel fut appel&eacute; &quot;Mistral 
        56&quot;. </p>
      <p>Le programme U55 ne vit que 2 A5smyfi courtes. La nouvelle longueur des 
        voitures, permettait de loger un 9e compartiment en premi&egrave;re et 
        d'inaugurer un chaudron d&eacute;di&eacute; &agrave; la 2nde classe avec 
        10 compartiments. Alors que l'&eacute;lectrification de Paris-Lille &eacute;tait 
        pr&eacute;vu pour 1959, la majorit&eacute; des voitures U56 furent d&eacute;di&eacute;es 
        &agrave; cet axe. En cons&eacute;quence, quatre A9, quatre voitures-bar 
        A5 et quatre voitures-restaurant A6 furent am&eacute;nag&eacute;es avec 
        un couloir central. Le nouveau bogie Y20H permettait, gr&acirc;ce &agrave; 
        un freinage renforc&eacute; de relever la vitesse limite &agrave; 150 
        km/h.</p>
      <p>A la fin des ann&eacute;es 50, les commandes de DEV Inox marqu&egrave;rent 
        le pas. Le nouveau fer de lance de la SNCF &eacute;tait les voitures UIC. 
      </p>
