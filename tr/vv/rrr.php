<p class="titre">RRR : Rames R&eacute;gionales R&eacute;versibles</p>
<p>Les RRR sont des voitures destin&eacute;es aux transports r&eacute;gionaux, construites entre 1985 et 1994. Elles sont largement issues des RIB et RIO (Rames Inox Omnibus) con&ccedil;ues dans les ann&eacute;es 1950, et dont elles sont g&eacute;n&eacute;ralement consid&eacute;r&eacute;es comme une version modernis&eacute;e. </p>
<p>Elles partagent avec ces derni&egrave;res les points communs suivants : 
  La construction en Acier inoxydable selon le proc&eacute;d&eacute; de soudure Budd, un gabarit r&eacute;duit avec plancher surbaiss&eacute;. Elles forment des &eacute;l&eacute;ments ind&eacute;formables de trois ou quatre voitures attel&eacute;es par barre. A une extr&eacute;mit&eacute;, une cabine de conduite permet de piloter une 
  locomotive en r&eacute;versibilit&eacute;. Deux &eacute;l&eacute;ments attel&eacute;s dans le m&ecirc;me sens peuvent &ecirc;tre tir&eacute;s/pouss&eacute;s par la locomotive.
  Comme les derniers programmes de construction des RIO, les RRR sont &eacute;quip&eacute;es de bogies Y 34 leur permettant une vitesse maximale de 140 km/h et le pup&icirc;tre 
  de conduite poss&egrave;de des commandes mixtes compatibles avec une locomotive &eacute;lectrique bicourant ou une locomotive Diesel.</p>
<p>En revanche, les RRR pr&eacute;sentent des diff&eacute;rences notables. La structure de caisse est diff&eacute;rente avec les portes d'acc&egrave;s coulissant &agrave; l'ext&eacute;rieur.
  Les extr&eacute;mit&eacute;s sont &eacute;quip&eacute;es de boucliers de chocs, recouverts par des masques en mat&eacute;riau composite fibre de verre / polyester leur donnant un aspect nettement plus moderne.
  Contrairement aux RIB et RIO qui avaient &eacute;t&eacute; con&ccedil;ues pour desservir des zones denses, les RRR sont am&eacute;nag&eacute;es pour des trajets plus longs et moins charg&eacute;s.
  Ainsi, chaque voiture ne poss&egrave;de que deux plateformes d'acc&egrave;s, et le confort est plus soign&eacute;, notamment l'isolation phonique et thermique.</p>
<p>A l'origine, les RRR furent propos&eacute;es en deux niveaux de confort et de capacit&eacute; en fonction de leur usage. Les segments de RRR P&eacute;riurbaines, destin&eacute;s &agrave; la
  desserte de la proche banlieue de grandes villes poss&eacute;daient des si&egrave;ges en vis &agrave; vis avec un pas de 1540 mm, gu&egrave;re plus confortable que les 1500 mm des RIO.<br />
  Les segments de RRR R&eacute;gionales poss&eacute;daient des si&egrave;ges en enfilade avec un pas de si&egrave;ge plus g&eacute;n&eacute;reux de 815 &agrave; 860 mm et quelques si&egrave;ges en vis &agrave; vis avec un
  pas de 1660 &agrave; 1785 mm. En revanche la capacit&eacute; des rames R&eacute;gionales chutait de 287 &agrave; 255 places et 20 strapontins pour un segment &agrave; trois caisses BDx + Bz + AB.<br />
  Ces am&eacute;nagements ont &eacute;t&eacute; revus lors de la modernisation des RRR par les conseils r&eacute;gionaux.</p>
<p>Les RRR sont compatibles avec des locomotives &eacute;lectriques (<a href="../9400.php" class="link1">BB 9600</a>, <a href="../16500.php" class="link1">BB 16500</a>, <a href="../8500.php" class="link1">BB 8500</a>, <a href="../25500.php" class="link1">BB 25500</a>, <a href="../22200.php" class="link1">BB 22200 RC</a>) ou Diesel (<a href="../66400.php" class="link1">BB 66400</a> , <a href="../67300.php" class="link1">BB 67300</a>, <a href="../67400.php" class="link1">BB 67400</a>).<br />
  Les <a href="../17000.php" class="link1">BB 17000</a> et <a href="../20200.php" class="link1">20200</a>, bien que compatibles, n'ont en principe pas &eacute;t&eacute; utilis&eacute;es en service commercial avec les RRR. </p>
