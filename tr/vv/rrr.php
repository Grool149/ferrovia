<p> Ces rames ont pu &ecirc;tre command&eacute;es et financ&eacute;es par les r&eacute;gions (41 &eacute;l&eacute;ments dont 9 &agrave; quatre caisses) ou par la SNCF (98 &eacute;l&eacute;ments dont 8 &agrave; quatre caisses).</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur : Carel et Fouch&eacute;<br />
Vitesse maximum autoris&eacute;e : 140 km/h</p>
<p>Longueur: 25,709 m pour les BDx et AB - 24,050 m pour les Bz et ABz interm&eacute;diaires<br />
Largeur de la caisse : 2,800 m<br />
Hauteur au-dessus du rail : 3,358 m<br />
Hauteur du plancher : 0,958 m<br />
Tare: 28t (Bz, ABz), 29t (AB), 32t (BDx) </p>
<p> 
Bogies 										: Y34 A et A1 ( roues de 800 mm de diam&egrave;tre, empattement de 2,500 m)</p>
