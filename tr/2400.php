      <p class="text">L'X 2400 est un autorail monocaisse unifi&eacute; de 600ch motoris&eacute; par deux moteurs de 300ch et de transmissions m&eacute;caniques situ&eacute;s &agrave; chaque extr&eacute;mit&eacute;s. La construction de 79 engins s'est &eacute;tal&eacute;e de 1951 &agrave; 1955. Ils &eacute;taient destin&eacute;s &agrave; assurer les relations r&eacute;gionales et interr&eacute;gionales &agrave; fort trafic. Ce r&ocirc;le leur a &eacute;t&eacute; repris par les <a href="/tr/2800.php" class="link1">X 2800</a> plus puissants.</p>
