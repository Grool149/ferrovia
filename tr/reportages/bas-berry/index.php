<p><b>07 Juin 2009 - Balade sur le chemin de fer touristique du Bas-Berry </b></p>
<p>Le TBB (<a href="http://www.traintouristiquedubasberry.com/" class="link1">http://www.traintouristiquedubasberry.com/</a>) est un chemin de fer touristique &agrave; 
voie m&eacute;trique qui exploite la partie d&eacute;class&eacute;e au sud de la ligne du Blanc-Argent. Son domaine se limite au nord &agrave; quelques dizaines de 
m&egrave;tres de la gare de Lucay-le-M&acirc;le, encore exploit&eacute;e par la SNCF et au sud par la gare d'Argy. La plateforme de la section Buzan&ccedil;ais-Argy 
&eacute;tant r&eacute;utilis&eacute;e par un embranchement &agrave; voie normale. La gare principale du TBB se situe &agrave; Ecueill&eacute;, ainsi que ses ateliers. 
Il a fallu 14 ans &agrave; l'association pour sauvegarder la ligne, la remettre en &eacute;tat et faire rouler son premier train.</p>
<p>Cette sortie fut organis&eacute;e par le club de mod&eacute;lisme de Conflans-Ste Honorine <a href="http://www.club-ccac.fr/" class="link1">CCAC</a> qui affr&ecirc;ta 
l'autorail Verney X224, le seul de la s&eacute;rie non modernis&eacute;, pour un aller-retour vers Lucay le matin et un aller-retour vers Argy l'apr&egrave;s-midi avec la 
remorque Verney XR 701. Un beau soleil nous a permis de ramener de tr&egrave;s beaux clich&eacute;s de cet autorail de 1950 dans son cadre buccolique.</p>
