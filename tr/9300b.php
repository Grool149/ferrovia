      <p class="text">Deux machines rev&ecirc;teront la livr&eacute;e &quot;Oullins&quot; 
        des 9200 (9313 et 9302). Cette derni&egrave;re machine est d'ailleurs 
        la seule manquante de la s&eacute;rie puisque radi&eacute;e en Ao&ucirc;t 
        1993. Le reste du parc a adopt&eacute; les diff&eacute;rentes versions 
        de livr&eacute;es b&eacute;ton puis Multiservice. En 2003, la 9322 puis 
        la 9318 viennent de sortir d'atelier en livr&eacute;e &quot;En Voyage&quot;.</p>
      <p class="text"> Ces machines restent les seules Jacquemin possédant encore 
        les vitres en demi-lune. Elles sont réputées pour leur fiabilité avec 
        moins de 10 incidents au million de kilomètres. Toutefois, leur confort 
        reste en deçà des séries de machines plus récentes. Aujourd'hui, on les 
        trouve en Aquitaine et plus rarement sur les m&ecirc;mes relations que 
        les 9200. </p>
