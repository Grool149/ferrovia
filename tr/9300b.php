      <p class="text">Cadettes de 10 ans des BB 9200, les 9300 furent construites 
        de 1967 à 1969, à 40 exemplaires. Elles regroupent toutes les modifications 
        qui seront apportées progressivement à leurs ainées: (commande de frein 
        PBL, frein &eacute;lectrique, cabines renforc&eacute;es, boites d'essieux 
        &agrave; rouleaux). De plus elles sont équipées de pantographes unijambistes 
        AM18 et de persiennes élargies. Leur champ d'action sera les grandes artères 
        sous courant continu du Sud-Ouest et du Sud-Est.</p>
      <p class="text">&nbsp; </p>
      <p class="text">Elles n'assureront cependant que peu de temps les trains 
        prestigieux (Mistral, Ligure) car les CC 6500 naissantes ont rapidement 
        pris le relais. L'arrivée des BB 7200 6 ans plus tard donne un coup terrible 
        à cette série qui n'a pourtant pas 10 ans d'existence. Elles sont alors 
        mutées progressivement au dépôt de Toulouse en remplacement des 2D2 5500.</p>
