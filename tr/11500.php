<p><span class="titre">Z 11500</span><br />
<span class="soustitre">Z2 Omnibus 25kV 50Hz</span></p>
<p>Les Z 11500 sont des &eacute;l&eacute;ments automoteurs monocourants 25kV alternatif 50Hz &agrave; deux caisses con&ccedil;us pour les services r&eacute;gionaux omnibus et construits &agrave; 22 exemplaires en 1986/88. Elles roulent principalement en Lorraine. Cinq &eacute;l&eacute;ments d&eacute;pendent de la r&eacute;gion  Alsace et la Z 11507 de Champagne Ardennes.</p>
<p>La composition de l'&eacute;l&eacute;ment est la Z 11500 proprement dite (motrice &agrave; adh&eacute;rence totale &agrave; quatre moteurs) et une ZR 111500. </p>
<p>Elles peuvent fonctionner en unit&eacute;s multiples jusqu'&agrave; trois &eacute;l&eacute;ments entres elles et avec les <a href="/tr/9500.php" class="link1">Z 9500</a> et <a href="/tr/9600.php" class="link1">Z 9600</a> qui composent le parc des &quot;Z2&quot;.</p>
<p>Les Chemins de Fer Luxembourgeois poss&egrave;dent &eacute;galement 22 Z2 monocourants tr&egrave;s proches des Z 11500, les '2000'. Elles se distinguent par l'absence de fourgon et de portes de cabines ainsi que des jupes plus courtes.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Francorail-ANF<br />
Tension d'alimentation:  monophas&eacute; 25 kV, 50 Hz<br />
Cha&icirc;ne de traction:  pont redresseur mixte<br />
Motorisation: 2 moteurs  &agrave; courant continu par bogie, 2 bogies moteurs sur la motrice.<br />
Puissance totale: 1200 kW</p>
<p>Longueur: 50,2m<br />
Masse: 109t</p>
  
