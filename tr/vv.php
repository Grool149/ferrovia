<p>Long fut le cheminement pour passer des premiers chariots &agrave; peine adapt&eacute;s au transport d'&ecirc;tres 
humains aux voitures &agrave; bogies de 26,400m Europ&eacute;ennes d'aujourd'hui. Les voitures ont v&eacute;cu de nombreuses sophistications au cours des 
deux derniers si&egrave;cles pour acqu&eacute;rir sans cesse un plus grand confort &agrave; des vitesses toujours sup&eacute;rieures.</span></p>
<p>Dans la seconde moiti&eacute; du XXe si&egrave;cle, elles semblent avoir atteint leur apog&eacute;e en Europe en proposant une construction 
m&eacute;tallique robuste, une suspension &agrave; deux &eacute;tages, la climatisation et une insonorisation remarquable compte tenu des vitesses 
qu'elles pratiquent au quotidien (160 voire 200 km/h).</p>
<p>Mais, en France, l'avenir des rames tract&eacute;es est scell&eacute;. Elles seront remplac&eacute;es par des automotrices, TGV en t&ecirc;te, de plus en plus omnipotentes. </p>
