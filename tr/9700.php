<p><span class="titre">BB 9700</span></p>
<p>Les BB 9700 sont 4 locomotives issues de la s&eacute;rie <a href="/tr/9200.php" class="link1">BB 9200</a> et transform&eacute;es pour la traction-pousse des rames V2N, en 1992. Elles ont &eacute;t&eacute; radi&eacute;es en 2007, remplac&eacute;es par des <a href="/tr/7200.php" class="link1">BB 7200</a> modifi&eacute;es pour ce m&ecirc;me service.</p>
<p>Les BB 9700 parcouraient la ligne Paris Gare de Lyon - Laroche Migennes -(Dijon).</p>
<p>Les modifications &eacute;taient proches de celles apport&eacute;es aux quinze <a href="/tr/16000.php" class="link1">BB 16000</a> transform&eacute;es en <a href="/tr/16100.php" class="link1">BB 16100</a> : ajout d'un automate et de la connectique pour recevoir les informations de traction et de freinage provenant de la voiture pilote V2N via un r&eacute;seau multiplex&eacute; g&eacute;n&eacute;ralis&eacute; (MUX-G), remplacement du pup&icirc;tre ant&eacute;-diluvien par un pup&icirc;tre ergonomique, livr&eacute;e sp&eacute;cifique reprenant les coloris Corail et qui n'&eacute;tait curieusement pas en accord avec le mat&eacute;riel remorqu&eacute; plus inspir&eacute; par le TGV Atlantique.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>BB 9271 transform&eacute;e en BB 9701<br />
BB 9281 transform&eacute;e en BB 9702<br />
BB 9285 transform&eacute;e en BB 9703<br />
BB 9286 transform&eacute;e en BB 9704</p>
<p>Constructeur : MTE - Creusot Loire - Jeumont Schneider<br />
  Tension d'alimentation: 1,5 kV Continu<br />
  Puissance en r&eacute;gime continu : 4 x 950 kW = 3800kW<br />
  Traction : 4 moteurs &agrave; courant continu <br />
  Cha&icirc;ne de traction: Banc rh&eacute;ostatique command&eacute; par arbre &agrave; cames JH.<br />
  Vitesse maximum autoris&eacute;e : 160 km/h</p>
<p>Longueur: 16,200 m<br />
  Masse: 82t</p>
