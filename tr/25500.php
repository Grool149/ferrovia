<p><span class="titre">BB 25500</span></p>
<p>Les BB 25500 sont des locomotives de moyenne puissance bicourantes, aptes &agrave; l'alimentation en courant continu sous 1,5 kV et en courant monophas&eacute; 50Hz sous 25kV.</p>
<p>Elles disposent de bogies monomoteur &agrave; double rapport d'engrenage manoeuvrable &agrave; l'arr&ecirc;t, de l'aptitude &agrave; l'Unit&eacute; Multiple et &agrave; la r&eacute;versibilit&eacute;, ce qui les rend polyvalentes. Elles peuvent ainsi tirer de lourds trains de marchandises ou tirer/pousser des trains l&eacute;gers r&eacute;gionaux ou de banlieue &agrave; arr&ecirc;ts fr&eacute;quents.</p>
<p>Les bogies &agrave; faible empattement, boites d'essieux internes et r&eacute;ducteur en porte-&agrave;-faux  n'ont pas une tr&egrave;s bonne tenue en voie, ce qui leur vaut le surnom de &quot;Danseuses&quot; comme  leurs &eacute;quivalents monocourant continu <a href="/tr/8500.php" class="link1">BB 8500</a> ou monophas&eacute; <a href="/tr/17000.php" class="link1">BB 17000</a>.</p>
<p>La livraison des 194 machines de la s&eacute;rie s'est &eacute;tal&eacute;e de 1964 &agrave; 1976 et d&eacute;roul&eacute;e en plusieurs &eacute;tapes. Plusieurs sous-s&eacute;ries pr&eacute;sentent des diff&eacute;rences notables de construction, dont le ch&acirc;ssis renforc&eacute; puis allong&eacute; ou les persiennes. Ainsi, &agrave; partir de la BB 25588, les machines poss&egrave;dent des cabines largement aggrandies et des persiennes filtrantes en inox.</p>
<p class="titre2">La carri&egrave;re</p>
<p>Les 25500 ont commenc&eacute; leur carri&egrave;re aux points de jonction entre le courant continu et le monophas&eacute; : Marseille, D&ocirc;le, la grande ceinture Parisienne. Sur la c&ocirc;te d'azur elles assurent un bref moment les rapides avant la livraison des premi&egrave;res <a href="/tr/25200.php" class="link1">BB 25200</a>. Dans le Jura, elles hissent en UM les lourds trains internationnaux jusque Vallorbe.</p>
<p>N&eacute;anmoins, leur utilisation principale sera la traction de train de marchandises et messagerie ou les express r&eacute;gionaux, et notamment avec les mat&eacute;riels r&eacute;versibles, rames Talbot puis RIO et RRR.</p>
<p>En Ile de France, elles se voient confier les trains de Paris-Montparnasse vers Dreux ou Mantes la Jolie form&eacute;s de VB2N. </p>
<p>Depuis 2008, les 25500 n'assurent plus de fret et sont remplac&eacute;es par des <a href="/tr/27000.php" class="link1">BB 27000</a>. Ensuite, ce sont les locomotives Transilien qui ont &eacute;t&eacute; chass&eacute;es par les <a href="/tr/27300.php" class="link1">BB 27300</a>.</p>
<p>En 2012, leur activit&eacute; r&eacute;gionale a &eacute;galement beaucoup baiss&eacute;e avec la radiation massive des RIO et l'arriv&eacute;e des automotrices TER de nouvelle g&eacute;n&eacute;ration. Pour les autres, Il reste un service encore actif en Bourgogne et Franche-Comt&eacute;, en PACA, en Alsace et Lorraine et en Bretagne.  Toutes les machines &agrave; petites cabines ont &eacute;t&eacute; radi&eacute;es. Douze d'entre elles ont &eacute;t&eacute; revendues en Roumanie. </p>
<p>Plusieurs machines ont &eacute;t&eacute; mut&eacute;es en r&eacute;gion Nord-Pas de Calais en remplacement des <a href="/tr/16500.php" class="link1">BB 16500</a> pour la traction/pousse des RIO, RRR et VR2N. Elles ne feront qu'un bref int&eacute;rim. Les RIO &eacute;tant amorties et des <a href="/tr/22200.php" class="link1">BB 22200 RC</a> leur prenant les missions VR2N. </p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom<br />
Tension d'alimentation: continu 1,5 kV, monophas&eacute; 25 kV 50 Hz<br />
Cha&icirc;ne de traction 1,5 kV: Banc rh&eacute;ostatique command&eacute; par graduateur<br />
Cha&icirc;ne de traction 25 kV: Graduateur HT, redresseurs silicium.<br />
Motorisation: 1 moteur par bogie, double rapport d'engrenage, transmission par anneau dansant.<br />
Puissance totale: 2940 kW<br />
Aptitude &agrave; l'UM et &agrave; la r&eacute;versibilit&eacute;</p>
<p>Longueur: 14,70m, 14,94m ou 15,57m <br />
Masse: 78,3t, 79,5t ou 79,7t <br />
Pantographes: 1 AM18B et 1 AM18U</p>
