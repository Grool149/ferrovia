<p><span class="titre">Z 9500 - Z 99500</span><br />
<span class="soustitre">Z2 Bicourant Express</span></p>
<p>Les Z 9500 sont des &eacute;l&eacute;ments automoteurs bicourants &agrave; deux caisses con&ccedil;us pour les services r&eacute;gionaux Express et construits &agrave; 18 exemplaires en 1982/83, auxquels s'ajoutent les deux &eacute;l&eacute;ments Z 99581 et 99582 identiques mais financ&eacute;es par la r&eacute;gion Franche Comt&eacute;. Elles roulent en Rh&ocirc;ne Alpes sur l'&eacute;toile de Savoie et en Franche Comt&eacute; entre Dijon et les lignes du Jura.</p>
<p>La composition de l'&eacute;l&eacute;ment est la Z 9500 proprement dite (motrice &agrave; adh&eacute;rence totale &agrave; quatre moteurs) et une ZR 19500. </p>
<p>Elles peuvent fonctionner en unit&eacute;s multiples jusqu'&agrave; quatre &eacute;l&eacute;ments entres elles et avec les <a href="/tr/7300.php" class="link1">Z 7300</a>, <a href="/tr/7500.php" class="link1">Z 7500</a>, <a href="/tr/9600.php" class="link1">Z 9600</a> et <a href="/tr/11500.php" class="link1">Z 11500</a> qui composent le parc des &quot;Z2&quot;.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Francorail-ANF<br />
Tensions d'alimentation: continu 1,5 kV et monophas&eacute; 25 kV, 50 Hz<br />
Cha&icirc;ne de traction: Rh&eacute;ostat sous courant continu, pont mixte sous monophas&eacute;<br />
Motorisation: 2 moteurs  &agrave; courant continu par bogie, 2 bogies moteurs sur la motrice.<br />
Puissance totale: 1275 kW</p>
<p>Longueur: 50,2m<br />
Masse: 116t</p>
