  </tr>
  <tr> 
    <td valign="top"> 
      <p class="text">Au cours des ann&eacute;es 60, la vapeur s'essouffle et 
        la voiture se d&eacute;mocratise. Pour survivre, le chemin de fer doit 
        &eacute;voluer et s'ouvrir &agrave; l'Europe. Mais pour assurer les nouveaux 
        trains Trans Europ Express, il faut du mat&eacute;riel rapide et luxueux. 
        Si les premi&egrave;res circulations internationales se contentent d'automoteurs 
        courts, l'ouverture du label TEE aux relations int&eacute;rieures prestigieuses 
        r&eacute;clament des moyens d'un autre ordre de grandeur. Pour tirer les 
        nouvelles et lourdes voitures TEE Inox et les futures Grand Confort, il 
        faut des locomotives puissantes. Les CC 6500 seront con&ccedil;ues pour 
        &ccedil;a.</p>
      <p class="text">Si elles n'innovent pas (elles reprennent le principe de 
        la machine continu &agrave; rh&eacute;ostat command&eacute; par graduateur 
        des <a href="/tr/9400.php" class="link1">BB 9400</a> et partagent de nombreux organes m&eacute;caniques des <a href="/tr/40100.php" class="link1">CC 
        40100</a> et <a href="72000.php" class="link1">CC 72000</a>), elles font dans 
        la d&eacute;mesure. 5900 kW, 200 km/h. Une disposition d'essieux CC sera 
        obligatoire pour emporter l'imposant &eacute;quipement &eacute;lectrique. 
        L'unique moteur de chaque bogie poss&egrave;de un inducteur divis&eacute; 
        en deux pour permettre trois couplages. Les premi&egrave;res machines 
        rev&ecirc;tent une livr&eacute;e aluminium &agrave; grands bandeaux rouge 
        et orange sign&eacute;e Arzens.</p>
      <p class="text">La seconde s&eacute;rie de ces machines est destin&eacute;e 
        &agrave; une t&acirc;che moins glorieuse mais tout aussi importante. La 
        rel&egrave;ve des machines Ex-PLM de la Maurienne n&eacute;cessite des 
        engins puissants pour tirer des tonnages importants sur des pentes de 
        plus de 30 pour mille. Les CC 6500 Maurienne disposeront du captage de 
        courant par 3e rail et seront limit&eacute;es &agrave; 100km/h au d&eacute;but 
        de leur carri&egrave;re jusqu'en 1976 lorsque la cat&eacute;naire est 
        pos&eacute;e. Une troisi&egrave;me sous-s&eacute;rie portera le nombre 
        d'engin &agrave; 74.</p>
      <p class="text">La mise au type 6500 des quatre CC 21000 compl&egrave;tera 
        la s&eacute;rie jusqu'&agrave; la CC 6578. Les CC 6500 m&egrave;neront 
        une carri&egrave;re riche et de forts kilom&eacute;trages &agrave; vitesse 
        &eacute;lev&eacute;e pour les machines du Sud-Ouest. Trains de prestige 
        &agrave; 200 km/h ou marchandises &agrave; tonnage important, elles s'acquittent 
        des t&acirc;ches les plus exigeantes gr&acirc;ce &agrave; leur double 
        rapport d'engrenages.</p>
      <p class="text">L'arriv&eacute;e des TGV Sud-Est puis Atlantique privent 
        les CC 6500 de leur services les plus prestigieux. Les nouvelles <a href="26000.php" class="link1">BB 
        26000</a> ach&egrave;vent d'expulser les CC 6500 vers le trafic Fret en 
        1998. Malgr&eacute; leur puissance appr&eacute;ciable sur les trains de 
        marchandises lourds, elles trainent une d&eacute;sagr&eacute;able r&eacute;putation 
        de ne pas m&eacute;nager les voies &agrave; cause de leur bogies C et 
        leur conception &eacute;litiste co&ucirc;te cher &agrave; l'activit&eacute; 
        Fret qui doit appliquer un aust&egrave;re plan de rigueur. En 2004, le 
        coup de th&eacute;atre intervient avec l'annonce d'une radiation massive 
        et pr&eacute;matur&eacute;e de la s&eacute;rie d'ici la fin de l'ann&eacute;e. 
        Un premier lot &eacute;tant d&eacute;j&agrave; arr&ecirc;t&eacute; depuis 
        Juin.</p>
      <p class="text">En 2005, seules 16 machines roulaient encore. 8 &eacute;taient conc&eacute;d&eacute;es 
        &agrave; l'activit&eacute; TER, rempla&ccedil;ant 5 autres machines confi&eacute;es 
        en 2004. Ces machines assuraient notamment les trains de pointe de sports 
        d'hiver en Maurienne assur&eacute;s en rame V2N. Plusieurs 6500 ont n&eacute;anmoins 
        pu &ecirc;tre sauvegard&eacute;es. La 6503 est d&eacute;sormais au musée 
        ferroviaire d'Augsburg en Allemagne. La 6530, qui a retrouv&eacute; une 
        superbe livr&eacute;e TEE inox, se trouve dans la collection compl&eacute;mentaire de la Cit&eacute; 
        du train. La 6572 devait &ecirc;tre conserv&eacute;e en raison de son bapt&egrave;me &quot;R&eacute;sistance Fer&quot;. Le mus&eacute;e a d&ucirc; se r&eacute;soudre &agrave; maquiller  la CC 6565, en meilleur &eacute;tat. Les CC 6534, 6549, 6559, 6570 et 6575 (ex-21001) sont pr&eacute;serv&eacute;es par des associations. La CC 6558 
        Maurienne, en service jusqu'en 2007, fut &eacute;galement conserv&eacute;e par l'APMFS.</p>
      <p class="text">Les CC 6570 et 6558 sont toujours en &eacute;tat de rouler et assurent r&eacute;guli&egrave;rement des trains sp&eacute;ciaux. </p>
      <p class="titre"><font size="3"><img src="../images/puce.gif" width="12" height="12"> 
        Les livr&eacute;es</font></p>
      <span class="text"> 
      <p>Les ann&eacute;es 60 consacre l'inox. Apr&egrave;s les rames de banlieue, 
        les voitures Mistral 56 (DEV Inox) et Mistral 69 (TEE Inox) y associent 
        une image de luxe. Aussi la premi&egrave;re s&eacute;rie des CC 6501 &agrave; 
        6538 rev&ecirc;t l'inox r&eacute;hauss&eacute; de rouge et d'orange. La 
        m&ecirc;me teinte Inox sera en outre appliqu&eacute;e &agrave; la livr&eacute;e 
        C160 des voitures DEV AO, UIC et USI ainsi que les rames RGP et RTG. La 
        mauvaise tenue de cette peinture oblige cependant &agrave; la remplacer 
        par un gris clair mat.</p>
      <p>Les machine de la seconde sous-s&eacute;rie (6539 - 6559) se voient sp&eacute;cialis&eacute;es 
        au service de la Maurienne et re&ccedil;oivent une livr&eacute;e plus 
        classique vert fonc&eacute; dite &quot;Maurienne&quot;. Les persiennes 
        sont d&eacute;sormais en polyester et d&eacute;cal&eacute;es. Ces machines 
        &eacute;tant banalis&eacute;es &agrave; partir de 1976 au reste de la 
        s&eacute;rie, elles recevront progressivement la livr&eacute;e TEE. La 
        6558 &agrave; re&ccedil;u &agrave; titre historique la livr&eacute;e Maurienne.</p>
      <p>La 3e sous-s&eacute;rie (6560 - 6574) re&ccedil;oit &agrave; nouveau 
        la livr&eacute;e TEE mais directement &agrave; base de gris mat. Les persiennes 
        sont en inox.</p>
      <p>Les 6512 et 6568 furent repeintes en livr&eacute;e b&eacute;ton en 1987. 
        Comme pour les <a href="15000.php" class="link1">BB 15000</a>, la forme 
        des persiennes imposait cependant de modifier le trac&eacute; du bandeau 
        orange par rapport aux <a href="7200.php" class="link1">BB 7200</a> et 
        <a href="/tr/22200.php" class="link1">22200</a>. Ce qui rendait l'esth&eacute;tique de la machine particuli&egrave;rement 
        triste. La 6512 a perdu cette livr&eacute;e au profit de la livr&eacute;e 
        Fret </p>
      <p>Puisque les 6500 &eacute;taient toutes affect&eacute;es au Fret &agrave; 
        partir de 1998, les derni&egrave;res machines pass&eacute;es en r&eacute;vision 
        ont rev&ecirc;tu la livr&eacute;e Fret.</p>
      </span> 
      <p class="titre"><font size="3"><img src="../images/puce.gif" width="12" height="12"> 
        Quelques donn&eacute;es techniques</font></p>
      <p class="text">Constructeur: Alsthom MTE<br>
        Tension d'alimentation: 1,5 kV continu<br>
        Cha&icirc;ne de traction: Banc rh&eacute;ostatique command&eacute; par 
        graduateur. <br>
        Motorisation: 1 moteur TTB 665 par bogie<br>
        Puissance totale: 5900 kW<br>
        <br>
        Longueur: 20,190 m<br>
        Masse: 115t (1e et 2e sous-s&eacute;rie), 118t (3e sous-s&eacute;rie)<br>
        Pantographes: 2 x AM 18 B</p>
      <p class="titre"><font size="3"><img src="../images/puce.gif" width="12" height="12"> 
        Machines remarquables de la s&eacute;rie</font></p>
      <span class="text"> 
      <p>La CC 6558 a retrouv&eacute; son ancienne livr&eacute;e Maurienne lors 
        de son dernier passage en r&eacute;vision. Elle est pr&eacute;serv&eacute;e par l'APMFS &agrave; Amb&eacute;rieu.      </p>
      <p>Les autres 6500 pr&eacute;serv&eacute;es : 6503 au mus&eacute;e d'Augsburg, la 6530 dans la collection de Mohon, les CC 6534 et 6559 par l'association Simil500 &agrave; Lyon, 6575 &agrave; N&icirc;mes, 6549 et 6558 par l'APMFS, 6565 &agrave; la Cit&eacute; du train (renum&eacute;rot&eacute;e 6572 avec le blason R&eacute;sistance Fer), 6570 pr&eacute;serv&eacute;e par l'APCC6570 en Avignon. </p>
      <p>La CC 6568 est de l'avis des amateurs le vilain petit canard de la s&eacute;rie. 
        Rev&ecirc;tant la livr&eacute;e b&eacute;ton, elle fut radi&eacute;e 
        ainsi.</p>
      <p>Les CC 6575 et 6576 sont les ex-21001 et 21002, elles sont reconnaissables 
        &agrave; leurs persiennes horizontales.</p>
      <p>Les CC 6577 et 6578 sont les ex-21003 et 21004.</p>
      <p>machines radi&eacute;es accidentellement: 6519, 41. De plus, 21 machines 
        &eacute;taient d&eacute;j&agrave; &eacute;t&eacute; ray&eacute;es des inventaires 
        en 2004.</p>
