<p class="titre">060 DA - CC 64000 - 65500<br />
<span class="soustitre">Les Dakota</span></p>
<p>Machines Di&eacute;sel-&eacute;lectriques de puissance moyenne construites &agrave; 35 exemplaires de 1955 &agrave; 1959. Rebaptis&eacute;es CC 64000 en 1962 puis CC 65500 en raison de la fabrication &agrave; plus de 500 exemplaires des <a href="/tr/63000.php" class="link1">BB 63500</a>, d&eacute;bordant sur la tranche suivante de num&eacute;rotation. <br />
Le son caract&eacute;ristique de leur moteur lent  leur a valu le surnom de &quot;Dakota&quot;.</p>
<p>Elles &eacute;taient utilis&eacute;es sur la grande ceinture parisienne au d&eacute;p&ocirc;t de la Plaine. Les premiers engins radi&eacute;s au d&eacute;but des ann&eacute;es 1980 furent vendus &agrave; des soci&eacute;t&eacute;s de travaux. Une bonne moiti&eacute; de l'effectif a connu une seconde carri&egrave;re en posant du ballast sur les chantiers de ligne &agrave; grande vitesse. Les derni&egrave;res ont particip&eacute; &agrave; la construction de la LGV M&eacute;diterran&eacute;e et furent radi&eacute;es en 2005. </p>
<p>La CC 65506 est pr&eacute;serv&eacute;e dans la r&eacute;serve de Mohon pour la Cit&eacute; du Train.</p>
