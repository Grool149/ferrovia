<p class="titre">BB 75000 - 75100 -  75300</p>
<p>99 locomotives (BB 75001 &agrave; 75099) limit&eacute;es au r&eacute;seau ferroviaire national.<br />
33 locomotives (BB 75101 &agrave; 75133) &eacute;quip&eacute;es des syst&egrave;mes de signalisation Allemagne et B&eacute;n&eacute;lux. </p>
<p>Une vingtaine de BB 75000 Akiem re&ccedil;oivent l'interphonie et sont renum&eacute;rot&eacute;es BB 75300. Elles sont lou&eacute;es &agrave; Intercit&eacute;s et doivent remplacer les <a href="/tr/67400.php" class="link1">BB 67400</a> vieillissantes sur les trains de nuit Paris-Brian&ccedil;on et Paris-rodez. Une livr&eacute;e carmillon est pr&eacute;vue. </p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom, Siemens, MTU<br />
Cha&icirc;ne de traction: Alternateur triphasé, moteurs de traction asynchrones <br />
Motorisation: moteur di&eacute;sel MTU 4000 R41 <br />
Puissance totale: 2000 kW</p>
