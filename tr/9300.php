<p><span class="titre">BB 9300</span></p>
<p>Cadettes de 10 ans des <a href="9200.php" class="link1">BB 9200</a>, les 9300 furent construites de 1967 &agrave; 1969, &agrave; 40 exemplaires. 
Elles sont dot&eacute;es de s&eacute;rie de toutes les modifications qui seront apport&eacute;es progressivement 
&agrave; leurs ain&eacute;es: (commande de frein PBL, frein &eacute;lectrique, cabines 
renforc&eacute;es, boites d'essieux &agrave; rouleaux). De plus elles 
sont &eacute;quip&eacute;es de pantographes unijambistes AM18 de persiennes &eacute;largies et de pup&icirc;tres ergonomiques. 
La construction de leur chassis fait appel &agrave; la soudure &eacute;lectrique. Ainsi la traverse avant est lisse l&agrave; o&ugrave; celle des BB 9200 est assembl&eacute;e par rivets.</p>
<p>Leur champ d'action sera les grandes art&egrave;res sous courant continu du Sud-Ouest 
et du Sud-Est. Elles n'assureront cependant que peu de temps les trains 
prestigieux (Mistral, Ligure) car les <a href="/tr/6500.php" class="link1">CC 6500</a> naissantes ont rapidement 
pris le relais. L'arriv&eacute;e des <a href="7200.php" class="link1">BB 7200</a> six ans plus tard modifie radicalement les affectations de cette s&eacute;rie qui n'a pourtant 
pas 10 ans d'existence. Elles sont alors mut&eacute;es progressivement au d&eacute;p&ocirc;t 
de Toulouse en remplacement des <a href="/tr/5500.php" class="link1">2D2 5500</a>.</p>
<p>Deux machines rev&ecirc;teront la livr&eacute;e &quot;Oullins&quot; 
des <a href="9200.php" class="link1">9200</a> (9313 et 9302). Cette derni&egrave;re 
machine est d'ailleurs la seule manquante de la s&eacute;rie puisque radi&eacute;e 
en Ao&ucirc;t 1993. Le reste du parc a adopt&eacute; les diff&eacute;rentes 
versions de livr&eacute;es b&eacute;ton puis Multiservice. En 2003, la 
9322, la 9318 puis la 9303 sont sorties d'atelier en livr&eacute;e &quot;En 
Voyage&quot;.</p>
<p>Ces machines restent les seules Jacquemin poss&eacute;dant encore 
les vitres en demi-lune. Elles sont r&eacute;put&eacute;es pour leur fiabilit&eacute; avec 
moins de 10 incidents au million de kilom&egrave;tres. Toutefois, leur confort 
reste en de&ccedil;&agrave; des s&eacute;ries de machines plus r&eacute;centes. Aujourd'hui, on les 
trouve en Aquitaine et plus rarement sur les m&ecirc;mes relations que 
les <a href="9200.php" class="link1">9200</a>.</p>
<p>La s&eacute;rie commence &agrave; &ecirc;tre radi&eacute;e au d&eacute;but des ann&eacute;es 2010. A la fin 2012, il ne reste que quatorze machine en service.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Jeumont MTE<br />
Tension d'alimentation: 1,5 kV Continu<br />
Motorisation: 1 moteur 750V de 962 kW par essieu<br />
Puissance totale: 3850 kW<br />
<br />
Longueur: 16,200 m<br />
Masse: 82t<br />
Pantographes: 2 x AM 18 B</p>
