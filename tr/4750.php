<p class="titre">X 4750 - X 4790 - XP 94750</p>
<p>Derni&egrave;re &eacute;volution des EAD, les X 4750 sont construits &agrave; 40 exemplaires (4750 &agrave; 4789), plus sept X 4790 (4790 &agrave; 4786) et huit XP 94750 (94750 &agrave; 94757). </p>
<p>Plus puissants que les <a href="/tr/4630.php" class="link1">X 4630</a>, les X 4750 et suivants disposent d'un moteur Saurer de 600 chevaux que l'on retrouve sur les <a href="/tr/2100.php" class="link1">X 2100</a> et <a href="/tr/2200.php" class="link1">2200</a>. Ce qui leur permet une vitesse limite augment&eacute;e &agrave; 140 km/h. La num&eacute;rotation des &eacute;l&eacute;ments est plus simple que celle de leurs ain&eacute;s. Une motrice X475n &eacute;tant associ&eacute;e &agrave; une remorque pilote XR 875n. </p>
<p>Les X 4790, tr&egrave;s proches, furent command&eacute;s par la r&eacute;gion Basse-Normandie pour la ligne Paris-Granville. Ils &eacute;taient am&eacute;nag&eacute;s avec des si&egrave;ges individuels et avaient en d&eacute;but de carri&egrave;re une livr&eacute;e sp&eacute;cifique orange. </p>
<p>Enfin les XP 94750 formaient les RAP (Rames Automotrices Postales). Ils assuraient le transport de conteneurs postaux sur les relations Paris-Caen, 
Paris-Rouen et Paris-Lille. Entre 1996 et 1998, sept motrices XP 94750 ont &eacute;t&eacute; transform&eacute;es en X 4750 avec cabines renforc&eacute;es 
et ajout&eacute;es au parc TER Lorraine. En revanche, les XRP 98750 furent radi&eacute;es et remplac&eacute;es par des remorques pilotes provenant d'<a href="/tr/4300.php" class="link1">X 4300</a> amortis. Seuls l'XP 94755 et l'XRP 98755 sont conserv&eacute;s en l'&eacute;tat au  Mus&eacute;e Postal des Anciens Ambulants de Toulouse, en gare de Toulouse-Raynal. <br />
Depuis l'int&eacute;gration des &eacute;l&eacute;ments transform&eacute;s, la s&eacute;rie compte 54 &eacute;l&eacute;ments (4750 &agrave; 4803).</p>
<p>Les &eacute;l&eacute;ments bourguignons ont &eacute;t&eacute; radi&eacute;s en 2011. Fin 2014, le parc affect&eacute; au TER Lorraine fut liquid&eacute;, les engins radi&eacute;s sont lou&eacute;s &agrave; RegioTrans en Roumanie. <br />
Ne restent, en service commercial en 2015, que les sept X 4790 toujours en activit&eacute; en Basse-Normandie et six &eacute;l&eacute;ments TER Haute-Normandie qui c&ocirc;toient les <a href="/tr/4900.php" class="link1">X 4900</a>. La Normandie est ainsi la derni&egrave;re r&eacute;gion o&ugrave; roulent quotidiennement des Caravelles. </p>
