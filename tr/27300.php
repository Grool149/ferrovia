<p><span class="titre">BB 27300</span><br />
<span class="soustitre">Les Prima de Banlieue</span></p>
<p>Machines d&eacute;riv&eacute;es de la &quot;Locomotives Fret&quot; <a href="/tr/27000.php" class="link1">BB 27000</a>. Les 27300 ont pour r&ocirc;le de faire l'int&eacute;rim entre l'amortissement des anciennes s&eacute;ries de locomotives utilis&eacute;es en banlieue (<a href="/tr/8500.php" class="link1">BB 8500</a>, <a href="/tr/17000.php" class="link1">BB 17000</a>, <a href="/tr/25500.php" class="link1">BB 25500</a>) &agrave; bout de souffle et celui des rames VB2N qui n'est pas pr&eacute;vu avant de longues ann&eacute;es encore. Cependant, l'avenir sera aux automotrices et d&egrave;s que ces rames seront hors service, les 27300 sont appel&eacute;es &agrave; reprendre un service d&eacute;di&eacute; au Fret comme leurs grandes soeurs. </p>
      <p>Parmi les &eacute;quipements sp&eacute;cifiques &agrave; cette sous-s&eacute;rie :  ligne de chauffage train, c&acirc;blots de r&eacute;versibilit&eacute;.</p>
      <p>60 exemplaires livr&eacute;s de 2006 &agrave; 2008, auxquels 7 locomotives suppl&eacute;mentaires ont &eacute;t&eacute; ajout&eacute;es en 2009 et 2010.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom<br />
Tension d'alimentation: continu 1,5 kV et monophas&eacute; 25 kV, 50 Hz<br />
Cha&icirc;ne de traction: onduleurs &agrave; transistors IGBT - Moteur asynchrones<br />
Motorisation: 2 moteurs suspendus par le nez par bogie<br />
Puissance totale: 4200 kW</p>
<p>Longueur: 19,520 m<br />
Masse: 90t<br />
Pantographes: 2</p>
  
