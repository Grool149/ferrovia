<p class="titre">Z 8800</p>
<p>Les Z 8800 sont des &eacute;l&eacute;ments automoteurs 
bicourants &agrave; deux niveaux con&ccedil;us pour le service de Banlieue Parisienne 
et construits &agrave; 58 exemplaires de 1984 &agrave; 1988. Elles roulent sur le RER C
 et la transversale La D&eacute;fense-La Verri&egrave;re (ligne U). Techniquement tr&egrave;s proches 
des <a href="5600.php" class="link1">Z 5600</a>, les motrices Z 8800 perdent 8 places assises remplac&eacute;es par 
le local transformateur qui augmente la tare de 4 tonnes.</p>
<p>Elles peuvent fonctionner en unit&eacute;s multiples entres elles et avec les <a href="5600.php" class="link1">Z 5600</a>, <a href="20500.php" class="link1">Z 20500</a> et <a href="20900.php" class="link1">Z 20900</a> qui composent le parc de "Z2N".</p>
<p>Les rames Z8800 sont d&eacute;sign&eacute;es par un num&eacute;ro suivi de la lettre "B" pour "bicourant".</p>
<p class="titre2">Composition des rames</p>
<p>Les rames ont une composition &agrave; quatre caisses : M+R+R+M.<br />
Les remorques sont des ZRB 25600 ou ZRAB 35600 num&eacute;rot&eacute;es dans le prolongement des remorques de Z 5600, qui sont identiques.</p>
<p>Si les motrices forment des couples stables (Z 8801-02 à Z 8915-16), les remorques sont réparties sans r&egrave;gle particuli&egrave;re.</p>
<p class="titre2">Quelques donn&eacute;es techniques</p>
<p>Constructeur: Alstom<br />
Tension d'alimentation: continu 1,5 kV et monophas&eacute; 25 kV, 50 Hz<br />
Chaîne de traction: Hacheur - Moteur s&eacute;rie &agrave; courant continu<br />
Motorisation: 2 moteurs  par bogie, 2 bogies moteurs par motrices.<br />
Puissance totale: 2800 kW</p>
<p>Longueur: 99m<br />
Masse: 224t</p>
<p class="titre2">Machines particuli&egrave;res de la s&eacute;rie</p>
<p>La Z 8895/96 fut le prototype d'une cha&icirc;ne de traction &agrave; base d'onduleurs de courant alimentant des moteurs asynchrones &agrave; cage d'&eacute;cureuil. Cette
cha&icirc;ne fut reprise pour la construction des <a href="20500.php" class="link1">Z 20500</a>.</p>
