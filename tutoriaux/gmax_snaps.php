      <p><span class="titre">Les snaps</span></p>
      <img src="../images/tuts/bases/snaps.gif" border="1" width="102" height="90">
<p class="text">Les snaps sont des aides au modélisateur. Ils lui servent &agrave; dessiner de fa&ccedil;on pr&eacute;cise. Les modes de snaps sont command&eacute;s par des boutons &agrave; bascule (toggles).</p>
      <p class="soustitre1"><font size="+1">Snaps 2D, 2,5D et 3D</font></p>
      <p class="text">Symbolis&eacute;s par un aimant, les snaps 2D, 2,5D et 3D servent &agrave; aimanter le curseur de la souris lors de la cr&eacute;ation d'objets ou de translations. Cette aimantation se fait sur un point exact ou sa projection sur l'&eacute;cran de gMax en fonction du snap s&eacute;lectionn&eacute;.</p>
      <p class="soustitre1"><font size="+1">Angle Snap Toggle</font></p>
      <p class="text">Ce snap permet de fixer des taux de rotation fixes.</p>
      <p class="soustitre1"><font size="+1">Percent Snap</font></p>
      <p class="text">Ce snap permet de fixer des taux de proportion fixes. Utilis&eacute; par les transformations d'&eacute;chelle.</p>
	<p class="soustitre1"><font size="+1">Le menu Grid and Snap Settings</font></p>
	<span class="text"><p>En cliquant droit sur l'ic&ocirc;ne du <b>Snap 3D</b>, le menu s'ouvre &agrave; l'onglet <b>Snaps</b>.</p>
	  <img src="../images/tuts/bases/snap_snaps.gif" border="1" width="288" height="253"> 
      <p>Les cases &agrave; cocher dans ce menu correspondent &agrave; des points particuliers sur lesquels le curseur peut &ecirc;tre aimant&eacute;. Dans la pratique, les plus utiles seront <b>Grid Points</b>, <b>Vertex</b>, <b>Endpoint</b> et <b>Midpoint</b>.</p>
	<P>En cliquant droit sur les ic&ocirc;nes <b>Angle Snap</b> ou <b>Percent Snap</b>, le menu s'ouvre &agrave; l'onglet <b>Options</b>.</p>
	  <img src="../images/tuts/bases/snap_options.gif" border="1" width="288" height="253"> 
      <p>Ce menu offre diff&eacute;rents r&eacute;glages comme la taille du marqueur de <b>Snap 3D</b>, la taille en pixel de la zone o&ugrave; le curseur de la souris sera attir&eacute;. On peut r&eacute;gler l'angle de rotation en degr&eacute; du <b>Angle Snap</b> ou bien le pourcentage du <b>Percent Snap</b>. Enfin, par d&eacute;faut, le <b>Snap 3D</b> ne prend pas en compte les contraintes d'axes lors des translations sauf si on lui impose en cochant la case <b>Translation/Use Axis Constraints</b>.</p>
	<p>L'onglet <b>Home Grid</b> permet essentiellement de param&eacute;trer la finesse du maillage de la grille. La valeur par d&eacute;faut vaut 10 m&egrave;tres. Pour mod&eacute;liser un v&eacute;hicule de chemin de fer, une valeur de 0,5 m&egrave;tres ou inf&eacute;rieure sera plus pratique.</p>
