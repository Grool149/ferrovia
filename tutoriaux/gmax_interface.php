      <p><span class="titre">L'interface de gMax</span></p>
      <span class="text">
      <p>L'interface de gMax est tr&egrave;s proche de celle de 3DStudio Max. 
        De nombreux livres sur ce logiciel seront donc tout &agrave; fait utiles 
        pour ma&icirc;triser gMax.<br>
        Toutefois voici les commandes qui seront les plus utiles &agrave; la cr&eacute;ation 
        d'add-ons pour MSTS.</p>
      <p>Tout d'abord, un aper&ccedil;u rapide de la fen&ecirc;tre de gMax. L'&eacute;cran 
        principal se divise en 4 vues que l'on pourra configurer comme on le voudra.<br>
        Il y a une barre de menus, une barre d'outils horizontale et un grand 
        panneau de commande vertical sur la droite. On trouve enfin un autre groupe 
        de boutons g&eacute;rant l'affichage et les animations en bas &agrave; 
        droite de l'&eacute;cran.</p>
      <p>Cette interface peut sembler aussi d&eacute;routante que le pup&icirc;tre 
        d'un 747. C'est pourquoi, il convient d'aborder chaque point pas &agrave; 
        pas. Et vous verrez que c'est finalement assez simple.</p>
