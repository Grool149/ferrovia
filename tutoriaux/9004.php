      <p class="titre">Mod&eacute;lisation de la BB 9004<font size="-1"> 
        (1ere partie)</font></p>
      <p align="center"><img src="../images/tr/BB9004-Reims-04062004.jpg" width="640" height="480" border="1"></p>
      <p class="text">Ce guide vise &agrave; mod&eacute;liser la carrosserie de 
        la BB 9004. Le premier paragraphe traite du probl&egrave;me assez g&eacute;n&eacute;ral 
        de la documentation.</p>
      <p class="soustitre1">La documentation</p>
      <span class="text"> 
      <p>Mod&eacute;liser fid&egrave;lement un objet comme une locomotive n&eacute;cessite 
        de bien se documenter sur l'objet r&eacute;el.</p>
      <p>Pour mod&eacute;liser une locomotive, deux types de documents peuvent 
        constituer le fond documentaire. Les plans et les photos. Chacun des deux 
        a ses avantages et inconv&eacute;nients qui sont compl&eacute;mentaires.</p>
      <p>Les plans sont assez peu &eacute;vidents &agrave; trouver (Dans le cas 
        pr&eacute;cis de la BB 9004, on pourra en trouver dans le Hors-s&eacute;rie 
        &quot;Le Train&quot; 3/95 consacr&eacute; en partie aux BB 9003 et 4, 
        et la fiche &quot;Loco-Revue&quot; n&deg;0131 parue dans le Hors-s&eacute;rie 
        de fiches documentaires n&deg;9) mais ils ont l'avantage de pr&eacute;senter 
        les profils et/ou faces des engins en projection orthogonale. Cependant, 
        un plan n'est pas la vraie machine. A cela plusieurs raisons possibles:<br>
        1) Le plan ne servait qu'&agrave; pr&eacute;senter la machine sans vouloir 
        &ecirc;tre particuli&egrave;rement fid&egrave;le (C'est bien souvent le 
        cas des plans de la DETE parus dans les hors-s&eacute;rie &quot;Le Train&quot; 
        qui ne sont l&agrave; que pour illustrer la nomenclature technique et 
        pr&eacute;senter assez grossi&egrave;rment les dimensions de la machine)<br>
        2) Le plan d'&eacute;tude ne correspond pas toujours parfaitement &agrave; 
        l'engin r&eacute;el. Il y a pu avoir des modifications entre le dessin 
        et la production, les diff&eacute;rentes sous-s&eacute;ries apportent 
        des modifications qui ne sont pas forc&eacute;ment reprises sur le plan. 
        Il faut donc bien analyser le plan pour s'assurer qu'il correspond &agrave; 
        la machine mod&eacute;lis&eacute;e.<br>
        3) Il ne s'agit pas d'un plan officiel. Dans ce cas, le projeteur a pu 
        commettre des erreurs selon son talent ou son d&eacute;sir de coller &agrave; 
        la machine r&eacute;elle (Cela semble &ecirc;tre le cas du plan de &quot;Loco-revue&quot;. 
        Cela dit, ce plan semble &ecirc;tre de bonne qualit&eacute; et assez fiable).</p>
      <p>Un plan c&ocirc;t&eacute; est par contre tr&egrave;s utile. Les photos 
        ne donnant aucune information sur les dimensions de l'engin. D'ailleurs 
        sur de nombreux plans trop peu fid&egrave;le, seules les c&ocirc;tes seront 
        r&eacute;ellement exploitables en compl&eacute;ment des photos.</p>
      <p>Les photos repr&eacute;sentent les machines r&eacute;elles. Mais, l&agrave; 
        aussi, il faut prendre quelques pr&eacute;cautions. Pour la m&ecirc;me 
        raison que les plans, il faut que la machine photographi&eacute;e soit 
        de la m&ecirc;me sous-s&eacute;rie et de la m&ecirc;me &eacute;poque que 
        la machine mod&eacute;lis&eacute;e, ou au moins conna&icirc;tre les diff&eacute;rences 
        qu'il faudra prendre en compte. Le principal d&eacute;faut des photographies 
        sont leur perspective qui d&eacute;forme les fuyantes. L'id&eacute;al 
        est donc de trouver des photos prises au t&eacute;l&eacute;objectif perpendiculairement 
        au sujet. Ceci att&eacute;nue les d&eacute;formations. Il faut cependant 
        disposer d'un bon recul et faire plusieurs clich&eacute;s le long de la 
        machine car les zones peu d&eacute;form&eacute;es se situent tout pr&egrave;s 
        de l'axe focal.</p>
      <p>Ce type de photos est assez rare car les photographes pr&eacute;f&egrave;rent 
        en g&eacute;n&eacute;ral prendre des 3/4 de la machine ou des photos en 
        ligne.<br>
        Il faut donc bien souvent faire soi-m&ecirc;me les photos ou se renseigner 
        aupr&egrave;s de mod&eacute;listes, qu'ils soient virtuels ou non. Les 
        photos prises d'angles divers ne sont toutefois pas &agrave; jeter. Car, 
        m&ecirc;me si elles ne serviront pas directement &agrave; la mod&eacute;lisation, 
        elles sont tr&egrave;s utiles pour bien se familiariser avec les formes 
        &agrave; reproduire et montrent des d&eacute;tails qui passent inaper&ccedil;us 
        sur les photos orthogonales.</p>
      <p>Dans le cas de la BB 9004, ses expositions successives &agrave; Paris-Bercy 
        (Journ&eacute;es du patrimoine en septembre 2002), sur les Champs-Elys&eacute;es 
        (op&eacute;ration &quot;Train Capitale&quot; en Mai-Juin 2003) et &agrave; 
        Reims (Juin 2004) m'ont permis de la photographier sous toutes les coutures.</p>
      <p>Reste la s&eacute;lection draconienne des clich&eacute;s. La partie la 
        plus d&eacute;licate &agrave; mod&eacute;liser est la caisse enveloppante 
        et particuli&egrave;rement le museau tout en courbes. La photo de d&eacute;part 
        est donc un clich&eacute; pris &agrave; environ 1,60 m du sol dans l'axe 
        du nez perpendiculairement &agrave; la voie. La forme des jupes est fid&egrave;le 
        mais le pare-brise et le toit, trop hauts, sont d&eacute;j&agrave; d&eacute;form&eacute;s.</p>
      </span> 
      <p class="soustitre1">Les lignes ma&icirc;tresses</p>
      <p></p>
      <span class="text"> 
      <p>La caisse pr&eacute;sente deux parties qui pourront &ecirc;tre mod&eacute;lis&eacute;es 
        distinctement. Nous allons commencer par les jupes vert fonc&eacute;. 
        La photo est appliqu&eacute;e en texture sur un rectangle. On s'assurera 
        que le mapping est proportionnel &agrave; la photo (commande <b>Bitmap 
        Fit</b> dans le modificateur <b>UVW Map</b>).</p>
      <p>Le rectangle est plac&eacute; et mis &agrave; l'&eacute;chelle. Sur la 
        figure suivante, le volume carr&eacute; est un gabarit aux dimensions 
        de l'essieu afin de s'assurer du bon placement du rectangle. On peut &eacute;galement 
        v&eacute;rifier &agrave; l'aide de d'objets <b>point</b> que les c&ocirc;tes 
        de la machine sont respect&eacute;es (toit &agrave; 3,65 m, tampon &agrave; 
        8,1 m de l'origine...etc). Une fois l'image correctement plac&eacute;e, 
        une spline est dessin&eacute;e en suivant le contour. 6 points (dont 4 
        pour le museau lui-m&ecirc;me) sont largement suffisants pour dessiner 
        le profil. La spline est ajust&eacute;e avec les poign&eacute;es des vertices. 
        Le vertex en bas est de type <b>B&eacute;zier-Corner</b> puisqu'il y a 
        rupture de continuit&eacute; des tangentes.</p>
      <p><img src="../images/tuts/creation/9004/1.jpg" border="1"> </p>
      <p>Une premi&egrave;re solution simple consiste &agrave; utiliser le modificateur 
        <b>Extrude</b>. La spline est extrud&eacute;e sur une largeur de 2980 
        mm.</p>
      <p><img src="../images/tuts/creation/9004/2.gif" width="568" height="371" border="1"></p>
      <p> A ce stade, le pas d'interpolation de la spline influence directement 
        le nombre de facettes du volume. Ci-dessus, ce pas est r&eacute;gl&eacute; 
        &agrave; 6, ce qui donne 92 facettes et 48 vertices. Nous obtenons une 
        finesse satisfaisante pour des vues rapproch&eacute;es mais surdimmensionn&eacute;e 
        si la locomotive se trouve &agrave; 200 m&egrave;tres de l'observateur. 
        Il faut donc penser aux LODs d&egrave;s maintenant. En diminuant le pas 
        d'interpolation &agrave; 2, le volume ne fait plus de 44 facettes et 24 
        vertices.</p>
      <p><img src="../images/tuts/creation/9004/3.gif" width="568" height="371" border="1"></p>
      <p><img src="../images/tuts/creation/9004/4.jpg" align="right" width="123" height="303" border="1"> 
        A partir de la forme obtenue, il faut arrondir les ar&egrave;tes vives 
        pour am&eacute;liorer l'aspect des jupes en vue rapproch&eacute;e. <b>Edit 
        Mesh</b> ne permet pas de faire de tels arrondis mais des chanfreins. 
        En faisant 2 chanfreins successifs, il est possible de lisser l'ar&egrave;te 
        suffisamment. Cependant les jupes de la 9004 posent un probl&egrave;me 
        insoluble. Le rayon de l'arrondi n'est pas constant le long de l'ar&egrave;te. 
        Alors qu'il ne fait qu'environ 2 cm en haut, il atteint environ 15 cm 
        en bas. Le passage d'un diam&egrave;tre &agrave; l'autre &eacute;tant 
        continu.</p>
      <p>Le modificateur <b>Extrude</b> s'av&egrave;re donc inefficace. Nous allons 
        construire une forme plus complexe avec l'aide des objets compos&eacute;s 
        <b>Loft </b>et <b>Shapemerge</b>. La spline d'origine est dupliqu&eacute;e 
        puis cach&eacute;e afin de travailler avec la copie. La spline est coup&eacute;e 
        en deux. La partie formant le passage de roues (rouge) est &eacute;tendue 
        de sorte qu'elle d&eacute;passe de la forme des jupes. La spline formant 
        les jupes (bleue) est prolong&eacute;e au niveau des essieux. Son premier 
        vertex est celui en haut &agrave; gauche..</p>
      <p><img src="../images/tuts/creation/9004/5.gif" width="449" height="239" border="1"></p>
      <p>La spline bleue servira de chemin (<b>Path</b>) au l'extrusion. Deux 
        nouvelles splines sont dessin&eacute;es pour devenir les formes (<b>Shape</b>) 
        extrud&eacute;es. Elles se r&eacute;sument toutes les deux &agrave; une 
        spline rectiligne de 2,980 m de large termin&eacute;e par un arc de 90&deg; 
        de chaque c&ocirc;t&eacute;. L'unique diff&eacute;rence &eacute;tant le 
        rayon de l'arc : 2 cm sur la premi&egrave;re et 15 cm sur la seconde. 
        Les pivots sont plac&eacute;s au centre de la spline.</p>
      <p><img src="../images/tuts/creation/9004/6.gif" width="334" height="195" border="1"></p>
      <p>A partir de la spline bleue, nous cr&eacute;ons l'objet <b>Loft</b> en 
        s&eacute;lectionnant la shape &quot;2 cm&quot;. Les param&egrave;tres 
        du <b>Loft</b> sont d&eacute;taill&eacute;es ci-dessous. Avec ces param&egrave;tres, 
        il y a 13 <b>Path Steps</b>. La Shape est attribu&eacute;e par d&eacute;faut 
        sur le step 0. Sur le step 1, cliquer sur Get Shape et res&eacute;lectionner 
        la shape &quot;2 cm&quot;. Tout le haut des jupes poss&egrave;de un arrondi 
        de 2 cm qui ne bougera plus. Sur le step 11, qui correspond &agrave; l'intersection 
        avec la spline rouge, cliquer sur Get Shape et s&eacute;lectionner la 
        shape &quot;15 cm&quot;. Entre ces deux positions sur le chemin, la forme 
        extrud&eacute;e va progressivement se modifier et l'arrondi va s'aggrandir 
        sans aucune cassure.</p>
      <p><img src="../images/tuts/creation/9004/7.gif" width="457" height="343" border="1"> 
        <img src="../images/tuts/creation/9004/8.gif" width="175" height="331"> 
      </p>
      <p>Un <b>Edit Mesh</b> est appliqu&eacute; &agrave; cette forme. Le c&ocirc;t&eacute; 
        est dessin&eacute; &agrave; la main en tendant un polygone. Les segments 
        sont orient&eacute;s (Turn) de fa&ccedil;on &agrave; le moins s'opposer 
        &agrave; la d&eacute;coupe des passages de roues.</p>
      <p><img src="../images/tuts/creation/9004/9.gif" width="447" height="343" border="1"> 
        <img src="../images/tuts/creation/9004/10.jpg" width="447" height="343" border="1"> 
      </p>
      <p>La forme sert de base &agrave; la cr&eacute;ation d'un objet compos&eacute; 
        <b>Shapemerge</b>. La Shape utilis&eacute;e pour la d&eacute;coupe &eacute;tant 
        bien s&ucirc;r la spline rouge. Apr&egrave;s un nouvel <b>Edit Mesh</b>, 
        les polygones en trop sont &eacute;barb&eacute;s et quelques vertices 
        inutiles sont fusionn&eacute;s avec leurs voisins. Voici l'&eacute;tat 
        de l'objet avant et apr&egrave;s ce petit travail de nettoyage.</p>
      <p><img src="../images/tuts/creation/9004/11.jpg" width="447" height="343" border="1"> 
        <img src="../images/tuts/creation/9004/12.gif" width="447" height="343" border="1"> 
      </p>
      <p>Par sym&eacute;trie la forme enti&egrave;re des jupes fait 424 faces 
        et 237 points, ce qui est raisonnable compte tenu de la complexit&eacute; 
        des surfaces.<br>
        Cependant nous ne garderons qu'un quart de cette forme pour l'instant. 
        La sym&eacute;trie ne sera r&eacute;alis&eacute;e qu'apr&egrave;s l'application 
        des textures, &eacute;galement sym&eacute;triques. Nous pouvons passer 
        &agrave; la suite.</p>
      <p><img src="../images/tuts/creation/9004/13.jpg" width="427" height="255" border="1"></p>
      <p>La seconde &eacute;tape concerne la partie haute de la caisse, assez 
        d&eacute;licate &agrave; r&eacute;aliser, puisqu'une simple extrusion 
        ne suffira pas pour mod&eacute;liser la forme complexe du toit (Le toit 
        est par ailleurs souvent la partie la plus difficile &agrave; mod&eacute;liser). 
        Sur la 9004, le galbe caract&eacute;ristique de la cabine est une difficult&eacute; 
        suppl&eacute;mentaire &agrave; relever. On peut ais&eacute;ment deviner 
        son profil dans l'axe m&eacute;dian de la machine, gr&acirc;ce &agrave; 
        la photo d&eacute;j&agrave; utilis&eacute;e pour les jupes. Mais le reflet 
        sur la caisse laisse &agrave; penser que le galbe s'accroit sur les c&ocirc;t&eacute;s. 
        Une photo de face donne le ligne d'intersection entre ce galbe et les 
        jupes. A d&eacute;faut d'indiquer directement la forme &agrave; reproduire, 
        cela nous informera des retouches &agrave; appliquer. Et, apr&egrave;s 
        quelques essais et t&acirc;tonnements, nous obtiendrons une reproduction 
        fid&egrave;le. </p>
      </span> 
