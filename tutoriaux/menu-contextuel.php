      <p class="titre">Bug des menus contextuels</p>
      <p class="text">Le clic droit au-dessus de la fen&ecirc;tre de travail de 
        gmax ouvre les menus contextuels. Il se peut que le gamepack Trainsim 
        perturbe le fonctionnement normal de ces menus les rendant illisibles.</p>
      <p class="text"><img src="../images/tuts/divers/bug_menu_contextuel_1.gif" width="513" height="396"></p>
      <p class="text">C'est un probl&egrave;me de police. Voici la marche &agrave; 
        suivre pour le r&eacute;gler.</p>
      <p class="text">Cliquer sur le menu <b>Customize</b> puis le sous menu <b>Customize 
        User Interface</b>.</p>
      <p class="text"><img src="../images/tuts/divers/bug_menu_contextuel_2.gif" width="215" height="289" border="1"></p>
      <p class="text"> Cela ouvre la fen&ecirc;tre suivante. Cliquer sur l'onglet 
        <b>Quads</b> puis, en bas &agrave; droite, sur le bouton <b>Advanced options</b>.</p>
      <p class="text"><img src="../images/tuts/divers/bug_menu_contextuel_3.gif" width="561" height="548" border="1"></p>
      <p class="text">Dans la fen&ecirc;tre qui s'ouvre, le menu d&eacute;roulant 
        <b>Menu Font</b> affiche probablement Arial Bold. Modifier en Arial ou 
        une autre police de votre choix. Les menus contextuels affichent d&eacute;sormais 
        les commandes lisiblement.</p>
      <p class="text"><img src="../images/tuts/divers/bug_menu_contextuel_4.gif" width="601" height="435" border="1"></p>
      <p class="link1">Ferrovia - 03 Mai 2003</p>
