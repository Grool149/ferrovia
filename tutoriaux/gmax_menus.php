      <p class="titre">La barre des menus</p>
      <p class="text">Les fonctions pr&eacute;sentes dans ces menus reprennent 
        souvent ce que les boutons proposent d&eacute;j&agrave;. Je ne vais donc 
        pas m'y attarder. Cependant, il y a l&agrave; quelques fonctions essentielles 
        &agrave; conna&icirc;tre:</p>
      <p class="soustitre1"><font size="+1">Menu File</font></p>
      <span class="text"><p>On y trouve les fonctions classiques de cr&eacute;ation 
        d'un nouveau projet, d'ouverture d'un projet existant, de sauvegarde, 
        d'importation de fichiers 3DStudio *.3ds. On y trouve &eacute;galement 
        la fonction <b>Merge</b> qui permet de fusionner deux projets gmax; utile 
        pour g&eacute;rer une biblioth&egrave;que d'objets standard tels que des 
        essieux, pantographes...etc.</p>
      <p>Une autre fonction tr&egrave;s int&eacute;ressante 
        est <b>Summary Info</b>. Elle liste tous les objets du projet est permet 
        de conna&icirc;tre le nombre de polygones utilis&eacute;s.</p></span>
      <p class="soustitre1"><font size="+1">Menu Group</font></p>
      <p class="text">Les fonctions de groupages permettent d'attacher temporairement 
        des objets entre eux pour qu'ils se d&eacute;placent comme un seul.</p>
      <p class="soustitre1"><font size="+1">Menu View</font></p>
      <p class="text">La fonction <b>Viewport background</b> permet d'afficher 
        une image en fond d'&eacute;cran. On peut donc afficher une photo, ou 
        mieux, un plan de l'objet que l'on mod&eacute;lise.</p>
      <p class="soustitre1"><font size="+1">Menu Customize</font></p>
      <p class="text">Ce menu propose diff&eacute;rents moyens pour param&eacute;trer 
        l'interface &agrave; son go&ucirc;t. Pour faire vite, on laissera ces 
        fonctions de c&ocirc;t&eacute; et travailler avec la configuration par 
        d&eacute;faut. Le menu <b>Customize</b> propose aussi les boites de dialogue 
        <b>Unit setup</b> et <b>Preferences</b> qu'il sera n&eacute;cessaire de 
        configurer pour ne pas avoir de mauvaise surprise dans MSTS.</p>
      <p class="soustitre1"><font size="+1">Menu Train Simulator</font></p>
      <p class="text">Le menu le plus important puisqu'il contient le <b>LOD 
        Manager</b> sans lequel gMax ne pourrait pas exporter de fichiers vers 
        MSTS</p>
