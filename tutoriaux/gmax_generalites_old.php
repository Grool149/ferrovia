      <p><span class="titre">gmax: G&eacute;n&eacute;ralit&eacute;s</span></p>
      <span class="text"><p>A quoi sert gmax? Comment se pr&eacute;sente-t-il? 
        et o&ugrave; peut-on le trouver? C'est &agrave; ces 3 questions que cette 
        page va r&eacute;pondre.</p>
      <p class="soustitre2">A quoi sert gmax?</p>
      <p>gmax est un logiciel d&eacute;riv&eacute; du modeleur 
        3D Studio Max de Discreet (anciennement Kinetix (anciennement Autodesk)). Il permet de cr&eacute;er des objets en 3 dimensions, de 
        les habiller avec des textures, de les animer...etc</p>
      <p>Sa principale diff&eacute;rence tient dans son utilisation. 
        3D Studio est un logiciel professionnel, grouillant de fonctionnalit&eacute;s, 
        cher et permettant de r&eacute;aliser de vrais films en images de synth&egrave;se. 
        gmax est d&eacute;volu au monde du jeu vid&eacute;o. Il existe deux versions. 
        La premi&egrave;re, destin&eacute;e aux d&eacute;veloppeurs de jeux permet 
        d'adapter gmax au jeu d&eacute;velopp&eacute;. La seconde, qui nous int&eacute;resse, 
        est gratuite et sert au joueur &agrave; &eacute;tendre les possibilit&eacute;s 
        du jeu. Dans le cas de Train Simulator, cela signifie de nouveaux trains, 
        b&acirc;timents ou d&eacute;cors divers.</p>
      <p>Les fonctionnalit&eacute;s de gmax sont &eacute;videmment bien plus restreintes 
        que celles de 3D Studio Max. gmax ne sait pas faire un rendu d'une sc&egrave;ne 
        3D. Il ne dispose pas non plus des techniques pointues de mod&eacute;lisation 
        de surfaces param&eacute;triques de son grand fr&egrave;re. Toutefois, 
        ces techniques ne sont pas vraiment int&eacute;ressantes pour faire de 
        la 3D temps r&eacute;el. Les créations pour jeu vidéo exigent un faible 
        nombre de polygones et seront r&eacute;alis&eacute;es par des techniques 
        de LPM (Low polys modelisation) pour lesquelles gmax est parfaitement 
        adapt&eacute;. De plus gmax ne dispose pas de formats d'export autres 
        que le format de sauvegarde *.gmax et les formats propres &agrave; chaque 
        jeu support&eacute;. Cela permet &agrave; Discreet d'emp&ecirc;cher l'utilisation 
        de gmax &agrave; des fins professionnelles.</p>
      <p class="soustitre2">Comment se pr&eacute;sente gmax?</p>
      <p>gmax se pr&eacute;sente comme 3D Studio Max avec une 
        interface tr&egrave;s semblable. Par contre, gmax seul n'est pas suffisant. 
        Chaque jeu poss&egrave;de des sp&eacute;cificit&eacute;s particuli&egrave;res. 
        Il est donc n&eacute;cessaire de patcher gmax. Ces patchs sont les Gamepacks. 
        </p>
      <p>Dans le cas particulier de MSTS, le gamepack g&egrave;re 
        les mat&eacute;riaux sp&eacute;cifiques &agrave; MSTS (&agrave; base de 
        textures *.ace, et collectant diverses informations telles que le shader 
        ou le Mip-Mapping Bias par exemple) et le LODManager (Module d'export 
        de fichiers *.s). </p>
